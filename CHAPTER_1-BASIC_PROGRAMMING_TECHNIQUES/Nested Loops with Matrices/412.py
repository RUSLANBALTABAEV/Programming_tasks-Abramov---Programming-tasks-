def choose_variant():
    """Выбор варианта условия (а-г)."""
    print("\nВыберите условие для b_i = 1:")
    print("а) все элементы i-й строки 1-й матрицы больше соотв. элементов 2-й")
    print("б) все элементы i-х строк обеих матриц отрицательны")
    print("в) i-е строки содержат вместе не более трёх положительных элементов")
    print("г) совпадают количества отрицательных и неотрицательных элементов в i-х строках")
    while True:
        var = input("Ваш выбор (а/б/в/г или a/b/v/g): ").strip().lower()
        mapping = {'а': 'a', 'a': 'a', 'б': 'b', 'b': 'b',
                   'в': 'c', 'v': 'c', 'c': 'c', 'г': 'd', 'g': 'd', 'd': 'd'}
        if var in mapping:
            return mapping[var]
        print("Некорректный ввод. Попробуйте ещё раз.")

def compute_b(n, mat1, mat2, variant):
    """Возвращает список b_1..b_n для выбранного варианта."""
    b = []
    for i in range(n):
        if variant == 'a':
            # все элементы строки mat1 > соответствующих в mat2
            ok = all(mat1[i][j] > mat2[i][j] for j in range(n))
            b.append(1 if ok else 0)
        elif variant == 'b':
            # все элементы обеих строк отрицательны
            ok = all(x < 0 for x in mat1[i]) and all(x < 0 for x in mat2[i])
            b.append(1 if ok else 0)
        elif variant == 'c':
            # суммарное количество положительных элементов в двух строках <= 3
            positives = sum(1 for x in mat1[i] if x > 0) + sum(1 for x in mat2[i] if x > 0)
            b.append(1 if positives <= 3 else 0)
        elif variant == 'd':
            # количества отрицательных и неотрицательных совпадают
            neg1 = sum(1 for x in mat1[i] if x < 0)
            nonneg1 = n - neg1
            neg2 = sum(1 for x in mat2[i] if x < 0)
            nonneg2 = n - neg2
            b.append(1 if (neg1 == neg2 and nonneg1 == nonneg2) else 0)
    return b

def main():
    n, mat1, mat2 = get_matrices()
    variant = choose_variant()
    b = compute_b(n, mat1, mat2, variant)

    variant_names = {
        'a': "все элементы i-й строки 1-й матрицы > соотв. элементов 2-й",
        'b': "все элементы i-х строк обеих матриц отрицательны",
        'c': "i-е строки содержат вместе не более трёх положительных элементов",
        'd': "совпадают количества отрицательных и неотрицательных элементов в i-х строках"
    }

    print("\n" + "=" * 60)
    print("РЕЗУЛЬТАТЫ")
    print("=" * 60)
    print("Первая матрица:")
    for row in mat1:
        print(" ".join(f"{x:4d}" for x in row))
    print("Вторая матрица:")
    for row in mat2:
        print(" ".join(f"{x:4d}" for x in row))

    print(f"\nУсловие: {variant_names[variant]}")
    print("Последовательность b:")
    for i, val in enumerate(b):
        print(f"  b_{i+1} = {val}")
    print("=" * 60)

if __name__ == "__main__":
    main()
