"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
6. Пошаговый ввод данных и вывод результатов *)

164. Даны действительные числа a1, ..., a50. Получить «сглаженные» значения a1, ..., a50, заменив в исходной последовательности все члены, кроме первого и последнего, по
формуле

ai = (ai-1 + ai + ai+1) / 3, i = 2,3, ..., 49;

считается, что
а) после того как получено новое значение некоторого члена, оно используется для вычисления нового значения следующего члена;
б) при «сглаживании» используются лишь старые значения членов.
"""


def main():
    n = 50  # Фиксированное количество чисел согласно условию
    original = [0.0] * n  # Создаем список для хранения исходных значений
    
    print(f"Введите {n} действительных чисел:")
    
    # Шаг 1: Ввод всех чисел
    for i in range(n):
        original[i] = float(input(f"a[{i+1}]: "))
    
    # Шаг 2: Метод а - использование обновленных значений
    print("\nМетод а (использование обновленных значений):")
    smoothed_a = original.copy()  # Копируем исходный массив
    
    for i in range(1, n-1):  # i от 1 до 48 (индексы 0..49)
        # Для вычисления используем уже обновленные значения предыдущих элементов
        smoothed_a[i] = (smoothed_a[i-1] + original[i] + original[i+1]) / 3
    
    # Вывод результатов метода а
    for i in range(n):
        print(f"a[{i+1}] = {smoothed_a[i]:.6f}")
    
    # Шаг 3: Метод б - использование только старых значений
    print("\nМетод б (использование только старых значений):")
    smoothed_b = original.copy()  # Копируем исходный массив
    
    for i in range(1, n-1):  # i от 1 до 48 (индексы 0..49)
        # Для вычисления используем только исходные значения
        smoothed_b[i] = (original[i-1] + original[i] + original[i+1]) / 3
    
    # Вывод результатов метода б
    for i in range(n):
        print(f"a[{i+1}] = {smoothed_b[i]:.6f}")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
