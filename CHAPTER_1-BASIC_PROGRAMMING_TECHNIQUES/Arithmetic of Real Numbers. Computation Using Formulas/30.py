"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЕМЫ ПРОГРАММИРОВАНИЯ
30. Дано действительное число x. Не пользуясь никакими
другими арифметическими операциями, кроме умножения, сложения и
вычитания, вычислить:

1 - 2 * x + 3 * x ** 2 - 4 * x ** 3 и 1 + 2 * x + 3 * x ** 2 + 4 * x ** 3.

Разрешается использовать не более восьми операций.
"""

def main():
    """Вычисляет два выражения с заданным x."""
    try:
        # Ввод данных
        x = float(input("Введите действительное число x: "))

        # Первое выражение
        result1 = 1 - 2 * x + 3 * x ** 2 - 4 * x ** 3

        # Второе выражение
        result2 = 1 + 2 * x + 3 * x ** 2 + 4 * x ** 3

        # Вывод результатов
        print(f"\nРезультат первого выражения: {result1:.2f}")
        print(f"Результат второго выражения: {result2:.2f}")

    except ValueError:
        print("Ошибка: введите корректное число.")

    input("\nНажмите любую клавишу, чтобы завершить программу.")


if __name__ == "__main__":
    main()
