"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЕМЫ ПРОГРАММИРОВАНИЯ
19. Определить время, через которое встретятся два тела,
равноускоренно движущиеся навстречу друг другу, если известны их
начальные скорости, ускорения и начальное расстояние между ними.
"""

def main():
    # Ввод данных
    v1 = float(input("Начальная скорость первого тела: "))
    a1 = float(input("Ускорение первого тела: "))
    v2 = float(input("Начальная скорость второго тела: "))
    a2 = float(input("Ускорение второго тела: "))
    s = float(input("Начальное расстояние между телами: "))

    # Проверка на положительное расстояние
    if s <= 0:
        print("Ошибка: начальное расстояние должно быть положительным!")
        return

    # Настройки метода половинного деления
    epsilon = 0.001  # точность вычислений
    t_min = 0.0
    t_max = 100.0  # можно увеличить, если тела очень медленные

    # Итеративный процесс метода половинного деления
    while t_max - t_min > epsilon:
        t_mid = (t_min + t_max) / 2
        s1 = v1 * t_mid + 0.5 * a1 * t_mid ** 2
        s2 = v2 * t_mid + 0.5 * a2 * t_mid ** 2
        dist_sum = s1 + s2

        if dist_sum < s:
            t_min = t_mid
        else:
            t_max = t_mid

    # Время встречи
    meeting_time = (t_min + t_max) / 2

    # Вывод результата
    print(f"Время встречи: {meeting_time:.2f} сек.")

    input("Нажмите любую клавишу, чтобы завершить программу.")


if __name__ == "__main__":
    main()
