"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЕМЫ ПРОГРАММИРОВАНИЯ
20. Найти сумму членов арифметической прогрессии a, a + d, ..., a + (n - 1) * d
по данным значениям a, d, n.
"""

def main():
    # Ввод данных
    a = float(input("Введите первый член прогрессии a: "))
    d = float(input("Введите разность прогрессии d: "))
    n = int(input("Введите количество членов прогрессии n: "))

    # Проверка корректности n
    if n <= 0:
        print("Ошибка: количество членов прогрессии должно быть положительным!")
        return

    # Вычисление суммы арифметической прогрессии
    S = (n / 2) * (2 * a + (n - 1) * d)

    # Вывод результата
    print(f"Сумма арифметической прогрессии: {S}")

    input("Нажмите любую клавишу, чтобы завершить программу.")


if __name__ == "__main__":
    main()
