"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЕМЫ ПРОГРАММИРОВАНИЯ
14. Определить силу притяжения F между телами массы m1 и m2, находящимися на расстоянии r друг от друга.
"""

# Константа гравитации
G = 6.674e-11

# Ввод данных
m1 = float(input("Масса первого тела (кг): "))
m2 = float(input("Масса второго тела (кг): "))
r = float(input("Расстояние между телами (м): "))

# Проверка радиуса
if r <= 0:
    print("Ошибка: радиус должен быть положительным числом!")
else:
    # Вычисление силы притяжения
    F = G * m1 * m2 / r ** 2
    # Вывод результата в научной нотации
    print(f"Сила притяжения между двумя телами: {F:.3e} Н")

input("Нажмите любую клавишу, чтобы завершить программу.")
