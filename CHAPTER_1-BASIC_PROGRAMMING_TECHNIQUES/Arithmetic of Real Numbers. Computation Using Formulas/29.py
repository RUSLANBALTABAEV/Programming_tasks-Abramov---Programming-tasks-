"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЕМЫ ПРОГРАММИРОВАНИЯ
29. Даны действительные числа x, y. Не пользуясь никакими
операциями, кроме умножения, сложения и вычитания, вычислить:

3 * x**2 * y**2 - 2 * x * y**2 - 7 * x**2 * y - 4 * y**2
+ 15 * x * y + 2 * x**2 - 3 * x + 10 * y + 6.

Разрешается использовать не более восьми умножений и восьми
сложений/вычитаний.
"""

def main():
    """Вычисляет значение выражения с ограниченным количеством операций."""
    try:
        # Ввод данных
        x = float(input("Введите действительное число x: "))
        y = float(input("Введите действительное число y: "))

        # Промежуточные вычисления (всего ≤ 8 умножений)
        x2 = x * x        # 1
        y2 = y * y        # 2
        xy = x * y        # 3
        x2y = x2 * y      # 4
        xy2 = x * y2      # 5
        x2y2 = x2 * y2    # 6

        # Основное выражение
        result = (3 * x2y2 - 2 * xy2 - 7 * x2y - 4 * y2 +
                  15 * xy + 2 * x2 - 3 * x + 10 * y + 6)

        # Вывод результата
        print(f"\nРезультат вычисления выражения: {result:.2f}")

    except ValueError:
        print("Ошибка: введите корректные действительные числа.")

    input("\nНажмите любую клавишу, чтобы завершить программу.")


if __name__ == "__main__":
    main()
