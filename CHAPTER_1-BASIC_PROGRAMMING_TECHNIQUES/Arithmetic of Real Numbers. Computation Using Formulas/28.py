"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЕМЫ ПРОГРАММИРОВАНИЯ
28. Дано действительное число x. Не пользуясь никакими другими
арифметическими операциями, кроме умножения, сложения и вычитания,
вычислить:

2 * x**4 - 3 * x**3 + 4 * x**2 - 5 * x + 6.

Разрешается использовать не более четырёх умножений и четырёх
сложений/вычитаний.
"""


def calculate_polynomial(x: float) -> float:
    """
    Вычисляет значение выражения:
    2x⁴ - 3x³ + 4x² - 5x + 6
    с использованием схемы Горнера.
    """
    # (((2x - 3)x + 4)x - 5)x + 6
    return (((2 * x - 3) * x + 4) * x - 5) * x + 6


def main():
    """Основная функция программы."""
    try:
        # Ввод данных
        x = float(input("Введите действительное число x: "))

        # Вычисление
        result = calculate_polynomial(x)

        # Вывод результата
        print(f"\nРезультат выражения при x = {x}: {result:.2f}")

    except ValueError:
        print("Ошибка: введите корректное число.")

    input("\nНажмите любую клавишу, чтобы завершить программу.")


if __name__ == "__main__":
    main()
