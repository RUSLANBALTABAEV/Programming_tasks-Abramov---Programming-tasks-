import math
import random


# ------------------------------------------------------------
# 1. Процедура проверки полуплоскости (по знаку векторного произведения)
# ------------------------------------------------------------
def cross_product(ax, ay, bx, by, cx, cy):
    """Векторное произведение (B-A) x (C-A)."""
    return (bx - ax) * (cy - ay) - (by - ay) * (cx - ax)


def same_half_plane(x1, y1, x2, y2, p1x, p1y, p2x, p2y):
    """
    Лежат ли точки (p1x, p1y) и (p2x, p2y) в одной полуплоскости
    (включая границу) относительно прямой (x1, y1)-(x2, y2).
    """
    val1 = cross_product(x1, y1, x2, y2, p1x, p1y)
    val2 = cross_product(x1, y1, x2, y2, p2x, p2y)
    return val1 * val2 >= -1e-9


# ------------------------------------------------------------
# 2. Процедура расстояния между двумя точками
# ------------------------------------------------------------
def distance(x1, y1, x2, y2):
    return math.hypot(x2 - x1, y2 - y1)


# ------------------------------------------------------------
# 3. Процедура площади треугольника по трём сторонам (Герон)
# ------------------------------------------------------------
def triangle_area_by_sides(x1, y1, x2, y2, x3, y3):
    a = distance(x1, y1, x2, y2)
    b = distance(x2, y2, x3, y3)
    c = distance(x3, y3, x1, y1)
    if a + b <= c or a + c <= b or b + c <= a:
        return 0.0
    p = (a + b + c) / 2.0
    return math.sqrt(p * (p - a) * (p - b) * (p - c))


# ------------------------------------------------------------
# 4. Процедура: находится ли точка внутри треугольника (включая границу)
# ------------------------------------------------------------
def point_in_triangle(px, py, x1, y1, x2, y2, x3, y3):
    """Проверяет, лежит ли точка (px,py) внутри треугольника (вкл. границу)."""
    return (same_half_plane(x1, y1, x2, y2, px, py, x3, y3) and
            same_half_plane(x2, y2, x3, y3, px, py, x1, y1) and
            same_half_plane(x3, y3, x1, y1, px, py, x2, y2))


# ------------------------------------------------------------
# Ввод координат
# ------------------------------------------------------------
def get_coordinates():
    """Выбор способа ввода координат двух треугольников (12 чисел)."""
    print("Задача 449: Вложенность треугольников и площадь заштрихованной области")
    print("Выберите способ ввода:")
    print("1 — Ручной ввод")
    print("2 — Случайная генерация")
    print("3 — Готовые примеры")

    while True:
        choice = input("Ваш выбор (1/2/3): ").strip()
        if choice in ('1', '2', '3'):
            break
        print("Ошибка: выберите 1, 2 или 3.")

    if choice == '1':
        while True:
            try:
                print("Введите 12 чисел (x1 y1 x2 y2 x3 y3 x4 y4 x5 y5 x6 y6) через пробел:")
                data = list(map(float, input().split()))
                if len(data) != 12:
                    print("Нужно ровно 12 чисел.")
                    continue
                return (data[0], data[1], data[2], data[3], data[4], data[5],
                        data[6], data[7], data[8], data[9], data[10], data[11])
            except ValueError:
                print("Ошибка ввода.")

    elif choice == '2':
        # Случайные треугольники. Гарантируем, что внутренний может быть вложен во внешний.
        # Сгенерируем внешний треугольник (побольше), а внутренний — как середины сторон внешнего (медиальный треугольник).
        while True:
            # случайные вершины внешнего треугольника
            ox1, oy1 = random.uniform(-10, 10), random.uniform(-10, 10)
            ox2, oy2 = random.uniform(-10, 10), random.uniform(-10, 10)
            ox3, oy3 = random.uniform(-10, 10), random.uniform(-10, 10)
            # проверим невырожденность
            if abs(cross_product(ox1, oy1, ox2, oy2, ox3, oy3)) < 1e-6:
                continue
            # внутренний треугольник: середины сторон внешнего (автоматически вложен)
            ix1, iy1 = (ox1 + ox2) / 2, (oy1 + oy2) / 2
            ix2, iy2 = (ox2 + ox3) / 2, (oy2 + oy3) / 2
            ix3, iy3 = (ox3 + ox1) / 2, (oy3 + oy1) / 2
            # округлим
            coords = (round(ix1, 2), round(iy1, 2), round(ix2, 2), round(iy2, 2), round(ix3, 2), round(iy3, 2),
                      round(ox1, 2), round(oy1, 2), round(ox2, 2), round(oy2, 2), round(ox3, 2), round(oy3, 2))
            print(f"Сгенерированы координаты (внутренний, затем внешний): {coords}")
            return coords

    else:  # готовые примеры
        examples = [
            # внутренний треугольник (1,1), (1,4), (4,4) — внутри внешнего (0,0), (5,0), (0,5)
            (1.0, 1.0, 1.0, 4.0, 4.0, 4.0,
             0.0, 0.0, 5.0, 0.0, 0.0, 5.0),
            # ещё один: внутренний (0.5, 0.5), (2,2), (0.5, 2) — внутри внешнего (0,0), (4,0), (0,4)
            (0.5, 0.5, 2.0, 2.0, 0.5, 2.0,
             0.0, 0.0, 4.0, 0.0, 0.0, 4.0),
            # не вложенный (внутренний выходит за пределы)
            (0.0, 0.0, 2.0, 0.0, 0.0, 2.0,
             1.0, 1.0, 2.0, 1.0, 1.0, 3.0)
        ]
        print("Готовые примеры:")
        for idx, vals in enumerate(examples, 1):
            print(f"{idx}: {vals}")
        while True:
            try:
                num = int(input("Выберите номер примера: "))
                if 1 <= num <= len(examples):
                    return examples[num - 1]
                else:
                    print(f"Номер от 1 до {len(examples)}.")
            except ValueError:
                print("Введите целое число.")


# ------------------------------------------------------------
# Основная программа
# ------------------------------------------------------------
def main():
    (x1, y1, x2, y2, x3, y3,
     x4, y4, x5, y5, x6, y6) = get_coordinates()

    # Проверка, что вершины первого треугольника лежат внутри второго
    vertices_inner = [(x1, y1), (x2, y2), (x3, y3)]
    all_inside = True
    for px, py in vertices_inner:
        if not point_in_triangle(px, py, x4, y4, x5, y5, x6, y6):
            all_inside = False
            break

    print("\nРезультаты:")
    print(f"Первый треугольник: ({x1}, {y1}), ({x2}, {y2}), ({x3}, {y3})")
    print(f"Второй треугольник: ({x4}, {y4}), ({x5}, {y5}), ({x6}, {y6})")

    if all_inside:
        area_outer = triangle_area_by_sides(x4, y4, x5, y5, x6, y6)
        area_inner = triangle_area_by_sides(x1, y1, x2, y2, x3, y3)
        shaded = area_outer - area_inner
        print("Первый треугольник полностью содержится во втором.")
        print(f"Площадь заштрихованной области: {shaded:.6f}")
    else:
        print("Первый треугольник НЕ содержится целиком во втором.")


if __name__ == "__main__":
    main()
    input("\nНажмите Enter, чтобы завершить программу.")
