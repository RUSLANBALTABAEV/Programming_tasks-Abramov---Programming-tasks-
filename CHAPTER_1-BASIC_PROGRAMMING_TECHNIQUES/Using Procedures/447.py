import math

# ------------------------------------------------------------
# Процедура: распознавание совершенных чисел
# ------------------------------------------------------------
def is_perfect_number(x):
    """
    Проверяет, является ли число x совершенным.
    Совершенное число равно сумме своих собственных делителей (например, 6 = 1+2+3).
    """
    if x <= 1:
        return False
    
    sum_of_divisors = 1  # 1 всегда является делителем для чисел > 1
    limit = math.isqrt(x) # Ищем делители до квадратного корня, чтобы не перебирать всё
    
    for i in range(2, limit + 1):
        if x % i == 0:
            sum_of_divisors += i
            other_divisor = x // i
            # Если делители разные (не квадратный корень), добавляем и его пару
            if other_divisor != i:
                sum_of_divisors += other_divisor
                
    return sum_of_divisors == x


# ------------------------------------------------------------
# Процедура поиска максимальной длины непрерывного отрезка
# (Идентична процедуре из задачи 440)
# ------------------------------------------------------------
def max_contiguous_length(arr, check_func):
    """
    Возвращает длину самой длинной подпоследовательности идущих подряд элементов,
    для которых check_func возвращает True.
    """
    max_len = 0
    cur_len = 0
    for val in arr:
        if check_func(val):
            cur_len += 1
        else:
            max_len = max(max_len, cur_len)
            cur_len = 0
    return max(max_len, cur_len)


