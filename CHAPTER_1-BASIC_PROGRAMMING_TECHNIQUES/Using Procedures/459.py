import math

# ------------------------------------------------------------
# Примеры процедур, соответствующих конкретным задачам §9
# ------------------------------------------------------------

def power(a, n):
    """Задача 317: вычисление a^n.
    Внутренний цикл накопления произведения заменён процедурой.
    """
    return a ** n

def sum_fraction_318(i):
    """Задача 318: вычисление суммы
    f(i) = 1/(i^2 + 1) + 1 / (i^2 + 2) + ... + 1/(i^2 + i + 1).
    Внутренний цикл суммирования вынесен в отдельную процедуру.
    """
    total = 0.0
    for k in range(1, i + 2):
        total += 1.0 / (i * i + k)
    return total

# ------------------------------------------------------------
# Процедуры, полезные для многих задач §9
# ------------------------------------------------------------

def is_prime(n):
    """Проверка простоты числа (задачи 321, 322, 325, 327 и др.)."""
    if n <= 1:
        return False
    for i in range(2, int(math.isqrt(n)) + 1):
        if n % i == 0:
            return False
    return True

def factorial(n):
    """Вычисление n! (задачи 319, 320, 324 и др.)."""
    res = 1
    for i in range(2, n + 1):
        res *= i
    return res

def inner_sum(x, m):
    """
    Сумма по внутреннему циклу для двойных сумм (задачи 322, 323 и подобные).
    Например, sum_{j = 1..m} 1 / (x + j).
    """
    s = 0.0
    for j in range(1, m + 1):
        s += 1.0 / (x + j)
    return s

def combination(n, k):
    """Число сочетаний C(n, k) (задачи на комбинаторику)."""
    return factorial(n) // (factorial(k) * factorial(n - k))

