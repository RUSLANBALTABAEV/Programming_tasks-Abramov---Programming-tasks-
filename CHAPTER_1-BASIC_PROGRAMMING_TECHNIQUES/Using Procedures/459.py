    x = 17
    print(f"3) is_prime({x}) = {is_prime(x)}   (проверка простоты, много задач)")

    # Факториал (задачи 319, 320)
    print(f"4) factorial(6) = {factorial(6)}   (задачи 319, 320 и др.)")

