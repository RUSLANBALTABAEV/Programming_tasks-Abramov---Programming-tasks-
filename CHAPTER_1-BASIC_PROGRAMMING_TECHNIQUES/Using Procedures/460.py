# ==========================================================
# а) Процедура для перевода строки десятичных цифр в целое число
# ==========================================================
def string_to_decimal(s):
    """
    Вычисляет целочисленное значение по строке s,
    являющейся записью числа в десятичной системе счисления.
    """
    if not s:
        raise ValueError("Входная строка не может быть пустой")
        
    result = 0
    start_index = 0
    is_negative = False
    
    # Обработка знака минус
    if s[0] == '-':
        is_negative = True
        start_index = 1
    elif s[0] == '+':
        start_index = 1

    for i in range(start_index, len(s)):
        char = s[i]
        if '0' <= char <= '9':
            # Преобразуем символ в число и накапливаем результат
            digit = ord(char) - ord('0')
            result = result * 10 + digit
        else:
            raise ValueError(f"Недопустимый символ '{char}' для десятичной системы")

    return -result if is_negative else result


# ==========================================================
# б) Процедура для перевода строки шестнадцатеричных цифр в целое число
# ==========================================================
def string_to_hexadecimal(s):
    """
    Вычисляет целочисленное значение по строке s,
    являющейся записью числа в шестнадцатеричной системе счисления (цифры и A-F).
    """
    if not s:
        raise ValueError("Входная строка не может быть пустой")
        
    result = 0
    start_index = 0
    is_negative = False
    
    # Обработка знака минус
    if s[0] == '-':
        is_negative = True
        start_index = 1
    elif s[0] == '+':
        start_index = 1

    for i in range(start_index, len(s)):
        char = s[i]
        digit = 0
        
        if '0' <= char <= '9':
            digit = ord(char) - ord('0')
        elif 'A' <= char <= 'F':
            digit = ord(char) - ord('A') + 10
        elif 'a' <= char <= 'f':
            digit = ord(char) - ord('a') + 10
        else:
            raise ValueError(f"Недопустимый символ '{char}' для шестнадцатеричной системы")
            
        result = result * 16 + digit

    return -result if is_negative else result


