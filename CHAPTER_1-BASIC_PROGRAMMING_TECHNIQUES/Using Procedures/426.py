import math
import random


def t(x):
    """
    Вычисляет t(x) как отношение двух сумм (k = 0..10).
    Используется накопительное вычисление степени и факториала для эффективности.
    """
    sum_num = 0.0   # числитель: Σ x^{2k+1} / (2k+1)!
    sum_den = 0.0   # знаменатель: Σ x^{2k} / (2k)!

    # Начальные значения для k=0
    pow_x = 1.0          # x^0
    fact = 1.0           # 0!
    # k=0
    sum_den += pow_x / fact           # x^0 / 0!
    sum_num += (pow_x * x) / (fact * (1))  # x^1 / 1!  (увеличиваем степень и факториал на 1)

    # Далее пересчёт для k=1..10
    for k in range(1, 11):
        # от x^{2k-1} к x^{2k} и факториал до (2k-1)!
        pow_x *= x * x           # теперь x^{2k}
        # факториал от (2k-1)! до (2k)!
        fact *= (2 * k - 1) * (2 * k)
        sum_den += pow_x / fact

        # числитель: следующая нечётная степень и факториал
        # от x^{2k} до x^{2k+1}
        sum_num += (pow_x * x) / (fact * (2 * k + 1))

    return sum_num / sum_den


def get_y():
    """Выбор способа ввода числа y."""
    print("Задача 426: Вычисление выражения с t(x)")
    print("Выберите способ ввода:")
    print("1 — Ручной ввод")
    print("2 — Случайная генерация")
    print("3 — Готовые примеры")

    while True:
        choice = input("Ваш выбор (1/2/3): ").strip()
        if choice in ('1', '2', '3'):
            break
        print("Ошибка: выберите 1, 2 или 3.")

    if choice == '1':
        while True:
            try:
                y = float(input("Введите действительное число y: "))
                return y
            except ValueError:
                print("Ошибка ввода. Повторите.")

    elif choice == '2':
        y = round(random.uniform(-3, 3), 2)
        print(f"Сгенерировано число y = {y}")
        return y

    else:  # готовые примеры
        examples = [0.0, 1.0, -1.0, 2.5, -0.5]
        print("Готовые примеры:")
        for idx, val in enumerate(examples, 1):
            print(f"{idx}: y = {val}")
        while True:
            try:
                num = int(input("Выберите номер примера: "))
                if 1 <= num <= len(examples):
                    return examples[num-1]
                else:
                    print(f"Номер должен быть от 1 до {len(examples)}.")
            except ValueError:
                print("Ошибка ввода. Введите целое число.")


def main():
    y = get_y()

    t1 = t(0.25)
    t2 = t(1 + y)
    t3 = t(y * y - 1)

    numerator = 1.7 * t1 + 2 * t2
    denominator = 6 - t3

    if abs(denominator) < 1e-12:
        print("Ошибка: знаменатель равен нулю, деление невозможно.")
    else:
        result = numerator / denominator
        print("\nПромежуточные значения:")
        print(f"  t(0.25)      = {t1:.6f}")
        print(f"  t(1 + y)     = {t2:.6f}")
        print(f"  t(y^2 - 1)   = {t3:.6f}")
        print(f"  Числитель    = {numerator:.6f}")
        print(f"  Знаменатель  = {denominator:.6f}")
        print(f"  Результат    = {result:.6f}")


if __name__ == "__main__":
    main()
    input("\nНажмите Enter, чтобы завершить программу.")
