import math
import random


# ------------------------------------------------------------
# Процедуры работы с рациональными числами
# ------------------------------------------------------------
def reduce_frac(num, den):
    """Полное сокращение дроби num/den. Возвращает (числитель, знаменатель)."""
    if den == 0:
        raise ValueError("Знаменатель не может быть равен нулю.")
    if den < 0:
        num, den = -num, -den
    if num == 0:
        return 0, 1
    g = math.gcd(abs(num), den)
    return num // g, den // g


def add_frac(f1, f2):
    """Сложение дробей f1 = (n1, d1) и f2 = (n2, d2)."""
    n1, d1 = f1
    n2, d2 = f2
    return reduce_frac(n1 * d2 + n2 * d1, d1 * d2)


def mul_frac(f1, f2):
    """Умножение дробей f1 и f2."""
    n1, d1 = f1
    n2, d2 = f2
    return reduce_frac(n1 * n2, d1 * d2)


def mul_int(f, k):
    """Умножение дроби f на целое число k."""
    n, d = f
    return reduce_frac(n * k, d)


def div_int(f, k):
    """Деление дроби f на целое число k (k ≠ 0)."""
    n, d = f
    return reduce_frac(n, d * k)


def neg_frac(f):
    """Противоположное число к f."""
    return -f[0], f[1]


def comb(n, k):
    """Биномиальный коэффициент C(n, k)."""
    if k < 0 or k > n:
        return 0
    return math.comb(n, k)


# ------------------------------------------------------------
# Вычисление v_n по двум законам
# ------------------------------------------------------------
def sequence_a(n):
    """
    Закон а): v0 = 1, v0 + C(k + 1, 1)v1 + ... + C(k + 1, k) vk = 0.
    Возвращает v_n в виде (числитель, знаменатель).
    """
    v = [(1, 1)]  # v0 = 1/1
    for k in range(1, n + 1):
        total = (0, 1)  # сумма C(k+1, i) * v_i для i = 0..k-1
        for i in range(k):
            coeff = comb(k + 1, i)
            term = mul_int(v[i], coeff)
            total = add_frac(total, term)
        # v_k = -total / C(k+1, k)
        vk = div_int(neg_frac(total), comb(k + 1, k))
        v.append(vk)
    return v[n]


def sequence_b(n):
    """
    Закон б): v0 = 1, v0 + C(2k, 2) v1 + C(2k, 4) v2 + ... + C(2k, 2k) vk = 0.
    Возвращает v_n в виде (числитель, знаменатель).
    """
    v = [(1, 1)]  # v0 = 1/1
    for k in range(1, n + 1):
        total = (0, 1)  # сумма C(2k, 2i) * v_i для i = 0..k - 1
        for i in range(k):
            coeff = comb(2 * k, 2 * i)
            term = mul_int(v[i], coeff)
            total = add_frac(total, term)
        # v_k = -total / C(2k, 2k)
        vk = div_int(neg_frac(total), comb(2 * k, 2 * k))
        v.append(vk)
    return v[n]


