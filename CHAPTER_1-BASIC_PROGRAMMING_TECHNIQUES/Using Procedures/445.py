import math
import random


def is_prime(x):
    """Процедура проверки простоты числа x."""
    if x <= 1:
        return False
    if x <= 3:
        return True
    if x % 2 == 0 or x % 3 == 0:
        return False
    i = 5
    while i * i <= x:
        if x % i == 0 or x % (i + 2) == 0:
            return False
        i += 6
    return True


def get_n():
    """Выбор способа ввода чётного числа n > 2."""
    print("Задача 445: Проверка гипотезы Гольдбаха")
    print("Выберите способ ввода:")
    print("1 — Ручной ввод")
    print("2 — Случайная генерация")
    print("3 — Готовые примеры")

    while True:
        choice = input("Ваш выбор (1/2/3): ").strip()
        if choice in ('1', '2', '3'):
            break
        print("Ошибка: выберите 1, 2 или 3.")

    if choice == '1':
        while True:
            try:
                n = int(input("Введите чётное целое число n (>2): "))
                if n <= 2 or n % 2 != 0:
                    print("n должно быть чётным и больше 2.")
                    continue
                return n
            except ValueError:
                print("Ошибка ввода.")

    elif choice == '2':
        # Генерируем случайное чётное число в разумном диапазоне
        n = random.randrange(4, 101, 2)
        print(f"Сгенерировано чётное число n = {n}")
        return n

    else:  # готовые примеры
        examples = [4, 6, 8, 10, 28, 100]
        print("Готовые примеры чётных n:", examples)
        while True:
            try:
                num = int(input("Выберите номер примера (1 - 6): "))
                if 1 <= num <= len(examples):
                    return examples[num - 1]
                else:
                    print("Номер от 1 до 6.")
            except ValueError:
                print("Введите целое число.")


def main():
    n = get_n()

    found = False
    for p in range(2, n // 2 + 1):
        if is_prime(p) and is_prime(n - p):
            print(f"\nГипотеза Гольдбаха для числа {n} выполняется:")
            print(f"{n} = {p} + {n - p}")
            found = True
            break

    if not found:
        print(f"\nДля числа {n} представление не найдено (противоречит известным данным).")


if __name__ == "__main__":
    main()
