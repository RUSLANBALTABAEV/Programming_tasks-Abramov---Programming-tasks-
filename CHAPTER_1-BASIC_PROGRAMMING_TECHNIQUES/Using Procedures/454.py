import math
import random


# ------------------------------------------------------------
# Рекурсивная процедура вычисления факториала
# ------------------------------------------------------------
def factorial_recursive(n):
    """Рекурсивное вычисление n! с базой 0! = 1! = 1."""
    if n <= 1:
        return 1
    return n * factorial_recursive(n - 1)


# ------------------------------------------------------------
# (Для сравнения) Нерекурсивная (итеративная) процедура
# ------------------------------------------------------------
def factorial_iterative(n):
    """Итеративное вычисление n! с помощью цикла."""
    result = 1
    for i in range(2, n + 1):
        result *= i
    return result


# ------------------------------------------------------------
# Ввод числа n
# ------------------------------------------------------------
def get_n():
    """Выбор способа ввода натурального числа n."""
    print("Задача 454: Вычисление факториала (рекурсивно)")
    print("Выберите способ ввода:")
    print("1 — Ручной ввод")
    print("2 — Случайная генерация")
    print("3 — Готовые примеры")

    while True:
        choice = input("Ваш выбор (1/2/3): ").strip()
        if choice in ('1', '2', '3'):
            break
        print("Ошибка: выберите 1, 2 или 3.")

    if choice == '1':
        while True:
            try:
                n = int(input("Введите натуральное число n (>=0): "))
                if n < 0:
                    print("Факториал определён для n >= 0.")
                    continue
                return n
            except ValueError:
                print("Ошибка ввода.")

    elif choice == '2':
        n = random.randint(0, 10)   # ограничим, чтобы не переполнять стек
        print(f"Сгенерировано n = {n}")
        return n

    else:
        examples = [0, 1, 5, 7, 10]
        print("Готовые примеры n:", examples)
        while True:
            try:
                num = int(input("Выберите номер примера (1-5): "))
                if 1 <= num <= len(examples):
                    return examples[num - 1]
                else:
                    print("Номер от 1 до 5.")
            except ValueError:
                print("Введите целое число.")


# ------------------------------------------------------------
# Основная программа
# ------------------------------------------------------------
def main():
    n = get_n()
    try:
        result = factorial_recursive(n)
        print(f"\n{n}! (рекурсивно) = {result}")
        # Сравним с итеративным (для проверки)
        print(f"{n}! (итеративно) = {factorial_iterative(n)}")
    except RecursionError:
        print("Ошибка: глубина рекурсии превышена. Используйте итеративный метод.")


if __name__ == "__main__":
    main()
    input("\nНажмите Enter, чтобы завершить программу.")
