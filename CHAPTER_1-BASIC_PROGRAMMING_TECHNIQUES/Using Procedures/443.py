import math
import random


# ------------------------------------------------------------
# Процедура: площадь треугольника по трём сторонам (формула Герона)
# ------------------------------------------------------------
def triangle_area(side1, side2, side3):
    """
    Вычисляет площадь треугольника по трём сторонам.
    Если треугольник не существует, возвращает 0.0.
    """
    if side1 + side2 <= side3 or side1 + side3 <= side2 or side2 + side3 <= side1:
        return 0.0
    p = (side1 + side2 + side3) / 2.0
    return math.sqrt(p * (p - side1) * (p - side2) * (p - side3))


# ------------------------------------------------------------
# Ввод данных
# ------------------------------------------------------------
def get_params():
    """Выбор способа ввода a, b, c, d."""
    print("Задача 443: Площадь пятиугольника (разбитого на три треугольника)")
    print("Рис. 18: треугольник1: стороны 1, a, 2;")
    print("         треугольник2: стороны 2, b, 2.5;")
    print("         треугольник3: стороны 2.5, c, d.")
    print("Выберите способ ввода:")
    print("1 — Ручной ввод")
    print("2 — Случайная генерация")
    print("3 — Готовые примеры")

    while True:
        choice = input("Ваш выбор (1/2/3): ").strip()
        if choice in ('1', '2', '3'):
            break
        print("Ошибка: выберите 1, 2 или 3.")

    if choice == '1':
        while True:
            try:
                print("Введите действительные числа a, b, c, d через пробел:")
                a, b, c, d = map(float, input().split())
                return a, b, c, d
            except ValueError:
                print("Ошибка ввода. Нужно 4 числа.")

    elif choice == '2':
        # Генерируем случайные стороны так, чтобы треугольники существовали
        # Для треугольника 1: стороны 1, a, 2 → условие: 1 + 2 > a  => a < 3 и a > |2 - 1| = 1 => a в (1,3)
        a = round(random.uniform(1.1, 2.9), 2)
        # Для треугольника 2: 2, b, 2.5 → 2 + 2.5 > b => b < 4.5 и b > 0.5
        b = round(random.uniform(0.6, 4.4), 2)
        # Для треугольника 3: 2.5, c, d → c + d > 2.5, 2.5 + c > d, 2.5 + d > c
        c = round(random.uniform(0.5, 5.0), 2)
        d = round(random.uniform(abs(2.5 - c) + 0.1, 2.5 + c - 0.1), 2)
        print(f"Сгенерированы: a = {a}, b = {b}, c = {c}, d = {d}")
        return a, b, c, d

    else:  # готовые примеры
        examples = [
            (1.5, 2.0, 3.0, 4.0),
            (2.0, 2.0, 2.5, 3.5),
            (1.8, 3.0, 4.0, 2.0),
        ]
        print("Готовые примеры (a, b, c, d):")
        for idx, vals in enumerate(examples, 1):
            print(f"{idx}: {vals}")
        while True:
            try:
                num = int(input("Выберите номер примера: "))
                if 1 <= num <= len(examples):
                    return examples[num - 1]
                else:
                    print(f"Номер от 1 до {len(examples)}.")
            except ValueError:
                print("Введите целое число.")


# ------------------------------------------------------------
# Основная программа
# ------------------------------------------------------------
def main():
    a, b, c, d = get_params()

    area1 = triangle_area(1.0, a, 2.0)      # треугольник 1
    area2 = triangle_area(2.0, b, 2.5)      # треугольник 2
    area3 = triangle_area(2.5, c, d)        # треугольник 3

    total = area1 + area2 + area3

    print("\nРезультаты:")
    print(f"Площадь треугольника 1 (1, a, 2): {area1:.6f}")
    print(f"Площадь треугольника 2 (2, b, 2.5): {area2:.6f}")
    print(f"Площадь треугольника 3 (2.5, c, d): {area3:.6f}")
    print(f"Общая площадь пятиугольника: {total:.6f}")


if __name__ == "__main__":
    main()
    input("\nНажмите Enter, чтобы завершить программу.")
