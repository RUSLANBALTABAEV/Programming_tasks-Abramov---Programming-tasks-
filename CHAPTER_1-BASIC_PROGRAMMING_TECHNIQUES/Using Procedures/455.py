import random


# ------------------------------------------------------------
# Рекурсивная процедура вычисления НОД (алгоритм Евклида)
# ------------------------------------------------------------
def gcd_recursive(n, m):
    """Рекурсивный НОД: НОД(n, m) = НОД(m, n % m), база – НОД(x, 0) = x."""
    if m == 0:
        return n
    return gcd_recursive(m, n % m)


# ------------------------------------------------------------
# Для сравнения: нерекурсивная (итеративная) процедура
# ------------------------------------------------------------
def gcd_iterative(n, m):
    """Итеративный НОД (алгоритм Евклида с циклом)."""
    while m != 0:
        n, m = m, n % m
    return n


# ------------------------------------------------------------
# Ввод чисел n и m
# ------------------------------------------------------------
def get_numbers():
    """Выбор способа ввода двух натуральных чисел."""
    print("Задача 455: Вычисление НОД (рекурсивно)")
    print("Выберите способ ввода:")
    print("1 — Ручной ввод")
    print("2 — Случайная генерация")
    print("3 — Готовые примеры")

    while True:
        choice = input("Ваш выбор (1/2/3): ").strip()
        if choice in ('1', '2', '3'):
            break
        print("Ошибка: выберите 1, 2 или 3.")

    if choice == '1':
        while True:
            try:
                n = int(input("Введите натуральное число n: "))
                m = int(input("Введите натуральное число m: "))
                if n <= 0 or m <= 0:
                    print("Числа должны быть натуральными (>=1).")
                    continue
                return n, m
            except ValueError:
                print("Ошибка ввода.")

    elif choice == '2':
        n = random.randint(1, 1000)
        m = random.randint(1, 1000)
        print(f"Сгенерированы числа: n = {n}, m = {m}")
        return n, m

    else:  # готовые примеры
        examples = [
            (12, 8), (17, 5), (100, 10), (1071, 462), (36, 48)
        ]
        print("Готовые примеры (n, m):")
        for idx, (n_val, m_val) in enumerate(examples, 1):
            print(f"{idx}: n = {n_val}, m = {m_val}")
        while True:
            try:
                num = int(input("Выберите номер примера: "))
                if 1 <= num <= len(examples):
                    return examples[num - 1]
                else:
                    print(f"Номер от 1 до {len(examples)}.")
            except ValueError:
                print("Введите целое число.")


# ------------------------------------------------------------
# Основная программа
# ------------------------------------------------------------
def main():
    n, m = get_numbers()
    try:
        result_rec = gcd_recursive(n, m)
        result_iter = gcd_iterative(n, m)
        print(f"\nНОД({n}, {m}) рекурсивно = {result_rec}")
        print(f"НОД({n}, {m}) итеративно = {result_iter}")
    except RecursionError:
        print("Ошибка: глубина рекурсии превышена. Используйте итеративный метод.")


if __name__ == "__main__":
    main()
    input("\nНажмите Enter, чтобы завершить программу.")
