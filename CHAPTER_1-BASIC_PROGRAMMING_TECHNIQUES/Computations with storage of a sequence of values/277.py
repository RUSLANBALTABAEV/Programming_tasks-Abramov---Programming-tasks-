"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
9. Вычисления с хранением последовательности значений

277. Даны действительные числа a1, …, an. Получить an, an–1,. .., a1*).
*) Каждый раз, когда число членов в данной последовательности
зависит от некоторой величины n, m,..., не отнесенных явно к данным
задачи, то подразумевается, что при работе с языками, в которых не
предусмотрены массивы с динамическими границами (таким языком
является, в частности, паскаль), эти величины либо определяются в
программе как константы (например, в программе на Паскале даются
определения const n=...; const m=... с конкретными числами вместо
многоточий), либо же еще до составления программы эти величины
заменяются конкретными числами. Если же язык позволяет после
ввода значений переменных n, m,..., рассматривать массив, границы
которого зависят от n, m,..., то следует воспользоваться этой
возможностью и считать значения n, m,..., данными числами.
"""


def main():
    # Ввод количества элементов
    n = int(input("Введите количество элементов n: "))
    
    # Ввод последовательности
    a = []
    for i in range(n):
        while True:
            try:
                value = float(input(f"Введите действительное число a_{i+1}: "))
                a.append(value)
                break
            except ValueError:
                print("Ошибка: введите действительное число (например, 3.14)")
    
    # Вывод исходной последовательности
    print("\nИсходная последовательность:")
    print(" ".join(f"{x:.2f}" for x in a))
    
    # Получение и вывод обратной последовательности
    reversed_a = list(reversed(a))
    print("\nОбратная последовательность:")
    print(" ".join(f"{x:.2f}" for x in reversed_a))
    
    # Альтернативный вывод с индексами
    print("\nПодробно:")
    for i in range(n):
        print(f"a_{i+1} = {a[i]:.2f}  ->  a_{n-i} (в обратном порядке) = {reversed_a[i]:.2f}")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
