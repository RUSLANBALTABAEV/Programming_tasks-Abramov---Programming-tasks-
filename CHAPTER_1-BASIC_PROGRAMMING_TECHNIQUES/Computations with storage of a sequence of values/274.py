"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
9. Вычисления с хранением последовательности значений

274. Даны действительные числа а1, …, а20. Получить числа b1, …, b20, где bi – среднее арифметическое всех членов последовательности а1, …, а20, кроме ai (i=1, 2,. .., 20).
"""


def main():
    n = 20
    a = []
    
    # Ввод 20 действительных чисел
    for i in range(n):
        while True:
            try:
                value = float(input(f"Введите действительное число a_{i+1}: "))
                a.append(value)
                break
            except ValueError:
                print("Ошибка: введите действительное число (например, 3.14)")
    
    # Вычисляем сумму всех чисел
    total_sum = sum(a)
    
    # Вычисляем b_i для каждого i
    b = [(total_sum - a[i]) / (n - 1) for i in range(n)]
    
    # Выводим результаты
    print("\nРезультаты:")
    print("i  |   a_i   |   b_i   ")
    print("-" * 25)
    for i in range(n):
        print(f"{i+1:2} | {a[i]:7.3f} | {b[i]:7.3f}")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
