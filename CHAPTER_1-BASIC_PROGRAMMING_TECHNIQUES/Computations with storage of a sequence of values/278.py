"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
9. Вычисления с хранением последовательности значений

278. Даны натуральные числа n1, …, n20, действительные числа x1, …, x20. Вычислить (n1 * x1 + ... + n20 * x20) / (n1 + ... + n20).
"""


def main():
    # Ввод 20 натуральных чисел n_i
    n = []
    for i in range(20):
        while True:
            try:
                val = int(input(f"Введите натуральное число n_{i+1}: "))
                if val <= 0:
                    print("Ошибка: число должно быть натуральным (положительным целым).")
                else:
                    n.append(val)
                    break
            except ValueError:
                print("Ошибка: введите целое положительное число.")

    # Ввод 20 действительных чисел x_i
    x = []
    for i in range(20):
        while True:
            try:
                val = float(input(f"Введите действительное число x_{i+1}: "))
                x.append(val)
                break
            except ValueError:
                print("Ошибка: введите действительное число (например, 3.14).")

    # Вычисление суммы произведений n_i * x_i
    sum_nx = sum(n[i] * x[i] for i in range(20))

    # Вычисление суммы n_i
    sum_n = sum(n)

    # Вычисление взвешенного среднего
    result = sum_nx / sum_n

    print(f"\nСумма произведений (n_i * x_i): {sum_nx}")
    print(f"Сумма весов (n_i): {sum_n}")
    print(f"Взвешенное среднее: {result:.6f}")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
