"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
9. Вычисления с хранением последовательности значений

288. Даны целые числа a1, …, an, каждое из которых отлично от
нуля. Если в последовательности отрицательные и положительные
члены чередуются (+, –, +, –, … или –, +, –, +, … ), то ответом должна
служить сама исходная последовательность. Иначе получить все
отрицательные члены последовательности, сохранив порядок их
следования.
"""


def main():
    # Ввод количества элементов
    n = int(input("Введите количество элементов n: "))
    
    # Ввод последовательности (все числа отличны от нуля)
    a = []
    for i in range(n):
        while True:
            try:
                value = int(input(f"Введите целое число a_{i+1} (не равное 0): "))
                if value == 0:
                    print("Ошибка: число должно быть отлично от нуля.")
                else:
                    a.append(value)
                    break
            except ValueError:
                print("Ошибка: введите целое число.")

    print(f"\nИсходная последовательность: {a}")

    # Проверяем, чередуются ли знаки
    # Для этого проверяем, чтобы произведение соседних элементов было отрицательным
    # (т.е. разные знаки)
    alternating = True
    for i in range(1, n):
        if a[i] * a[i-1] > 0:  # одинаковые знаки
            alternating = False
            break

    if alternating:
        print("Знаки в последовательности чередуются.")
        print("Результат (исходная последовательность):")
        result = a
    else:
        print("Знаки в последовательности НЕ чередуются.")
        # Получаем все отрицательные члены, сохраняя порядок
        result = [x for x in a if x < 0]
        print("Результат (только отрицательные члены):")

    # Выводим результат
    if result:
        for i, val in enumerate(result, 1):
            print(f"b_{i} = {val}")
        print(f"\nСписок: {result}")
    else:
        print("Результат: пустая последовательность")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
