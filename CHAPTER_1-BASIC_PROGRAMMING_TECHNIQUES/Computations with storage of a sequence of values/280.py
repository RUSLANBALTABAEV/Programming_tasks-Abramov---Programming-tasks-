"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
9. Вычисления с хранением последовательности значений

280. Пусть xi, yi (i=1, 2,. ..) определены, как в задаче 167.
Получить x1, …, x25, y1, …, y25.
"""


def main():
    n = 25  # количество элементов
    
    # Инициализируем массивы
    x = [0] * n
    y = [0] * n
    
    # Начальные условия
    x[0] = 1  # x1
    y[0] = 1  # y1
    x[1] = 2  # x2
    y[1] = 2  # y2
    
    # Вычисляем факториал итеративно
    factorial = 1  # 0! = 1
    for i in range(1, n + 1):  # i от 1 до 25
        factorial *= i  # i! = (i-1)! * i
        
        # Для i >= 3 вычисляем x_i и y_i
        if i >= 3:
            # i в математической записи, в коде индекс = i-1
            index = i - 1  # индекс для x_i и y_i
            
            # Вычисляем x_i = (y_{i-1} - y_{i-2}) / i
            x[index] = (y[index-1] - y[index-2]) / i
            
            # Вычисляем y_i = (x_{i-1}^2 + x_{i-2} + y_{i-1}) / i!
            # x[index-1] = x_{i-1}, x[index-2] = x_{i-2}, y[index-1] = y_{i-1}
            numerator = x[index-1]**2 + x[index-2] + y[index-1]
            y[index] = numerator / factorial
    
    # Вывод результатов
    print("Последовательности x и y:")
    print("i\tx_i\t\ty_i")
    print("-" * 40)
    for i in range(n):
        print(f"{i+1}\t{x[i]:.10f}\t{y[i]:.10f}")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
