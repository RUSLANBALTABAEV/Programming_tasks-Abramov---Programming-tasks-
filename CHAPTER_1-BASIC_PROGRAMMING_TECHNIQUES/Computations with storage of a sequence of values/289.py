"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
9. Вычисления с хранением последовательности значений

289. Даны натуральное число m, действительные числа a1, …, a30 (числа a1, …, a30 попарно различны, m <= 30).
В последовательности a1 ,…, a30 поменять местами наибольший член и член с номером m.
"""


def main():
    # Ввод натурального числа m (1 ≤ m ≤ 30)
    while True:
        try:
            m = int(input("Введите натуральное число m (1 ≤ m ≤ 30): "))
            if 1 <= m <= 30:
                break
            else:
                print("Ошибка: m должно быть в диапазоне от 1 до 30.")
        except ValueError:
            print("Ошибка: введите целое число.")

    # Ввод 30 действительных чисел (попарно различных)
    a = []
    for i in range(30):
        while True:
            try:
                value = float(input(f"Введите действительное число a_{i+1}: "))
                if value in a:
                    print("Ошибка: все числа должны быть различными.")
                else:
                    a.append(value)
                    break
            except ValueError:
                print("Ошибка: введите действительное число.")

    print(f"\nИсходная последовательность:")
    for i in range(30):
        print(f"a_{i+1} = {a[i]}")

    # Находим индекс максимального элемента (нумерация с 0)
    max_index = a.index(max(a))
    max_value = a[max_index]
    m_index = m - 1  # преобразуем номер m в индекс (нумерация с 0)

    print(f"\nМаксимальный элемент: a_{max_index + 1} = {max_value}")
    print(f"Элемент с номером m: a_{m} = {a[m_index]}")

    # Меняем местами максимальный элемент и элемент с номером m
    a[max_index], a[m_index] = a[m_index], a[max_index]

    print(f"\nПоследовательность после замены:")
    for i in range(30):
        print(f"a_{i+1} = {a[i]}")

    # Дополнительно выделим измененные элементы
    print(f"\nИзменения:")
    print(f"Было: a_{max_index + 1} = {max_value}, a_{m} = {a[max_index]}")
    print(f"Стало: a_{max_index + 1} = {a[max_index]}, a_{m} = {max_value}")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
