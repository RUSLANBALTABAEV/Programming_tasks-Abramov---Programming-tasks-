"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
9. Вычисления с хранением последовательности значений

276. Построить последовательность целых чисел а1, …, а30, где a1=1, a2=1; ai = a[i/2] + ai–2 (i = 3,..., 30).
"""


def main():
    n = 30
    a = [0] * n
    
    # Инициализация первых двух элементов
    a[0] = 1  # a1
    a[1] = 1  # a2
    
    # Вычисляем остальные элементы
    for i in range(2, n):  # i соответствует индексу i-1 (так как индексация с 0)
        # a_i = a_{floor(i/2)} + a_{i-2}
        # Внимание: в нашей нумерации (с 0) индекс для floor(i/2) нужно пересчитать
        # У нас есть i (считая с 0), но формула использует номер элемента (начиная с 1)
        # Для элемента a[i] (который будет a_{i+1} в условии):
        # Нужно вычислить a_{floor((i+1)/2)} и a_{(i+1)-2} = a_{i-1}
        # Причем floor((i+1)/2) - 1 - это индекс в массиве
        
        # Вычисляем индекс для a_{floor((i+1)/2)}
        index_floor = (i + 1) // 2 - 1  # -1 потому что индексация с 0
        # Индекс для a_{i-1} (это просто i-1)
        index_prev = i - 1
        
        a[i] = a[index_floor] + a[index_prev]
    
    # Выводим последовательность
    print("Последовательность a_1, ..., a_30:")
    for i in range(n):
        print(f"a_{i+1} = {a[i]}")
    
    # Также выведем в одну строку для компактности
    print("\nВ одну строку:")
    print(", ".join(str(x) for x in a))

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
