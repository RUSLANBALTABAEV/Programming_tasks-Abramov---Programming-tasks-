"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
9. Вычисления с хранением последовательности значений

297. Даны целые числа a1,…, a100. Получить новую последовательность из 100 целых чисел, заменяя ai нулями, если |ai| не равно max(a1, ..., a100), и заменяя ai, единицей в противном случае (i = 1, ..., 100).
"""


import random

def main():
    print("Программа для преобразования последовательности целых чисел.")
    print("Условие: заменить a_i на 0, если |a_i| ≠ max(|a_1|, ..., |a_100|),")
    print("иначе заменить a_i на 1.")
    
    # Выбор способа ввода данных
    print("\nВыберите способ задания последовательности:")
    print("1. Ввести числа вручную")
    print("2. Использовать случайные числа от -100 до 100")
    print("3. Использовать готовый набор 1: числа от 1 до 100")
    print("4. Использовать готовый набор 2: числа от -50 до 49")
    print("5. Использовать готовый набор 3: специальный набор с одинаковыми максимумами по модулю")
    
    while True:
        try:
            choice = int(input("\nВаш выбор (1-5): "))
            if 1 <= choice <= 5:
                break
            else:
                print("Ошибка: выберите число от 1 до 5.")
        except ValueError:
            print("Ошибка: введите целое число от 1 до 5.")
    
    n = 100  # количество элементов по условию
    
    # Инициализация последовательности в зависимости от выбора
    if choice == 1:
        # Ручной ввод
        print(f"\nВведите {n} целых чисел:")
        a = []
        for i in range(n):
            while True:
                try:
                    value = int(input(f"a_{i+1}: "))
                    a.append(value)
                    break
                except ValueError:
                    print("Ошибка: введите целое число.")
    
    elif choice == 2:
        # Случайные числа от -100 до 100
        random.seed()
        a = [random.randint(-100, 100) for _ in range(n)]
        print(f"\nИспользуется набор из {n} случайных чисел от -100 до 100")
    
    elif choice == 3:
        # Готовый набор 1: числа от 1 до 100
        a = list(range(1, n+1))
        print("\nИспользуется готовый набор 1: числа от 1 до 100")
    
    elif choice == 4:
        # Готовый набор 2: числа от -50 до 49
        a = list(range(-50, 50))
        print("\nИспользуется готовый набор 2: числа от -50 до 49")
    
    elif choice == 5:
        # Готовый набор 3: специальный набор
        # Создаем набор, где максимальное по модулю значение (100) встречается несколько раз
        a = []
        for i in range(n):
            if i % 10 == 0:  # каждый 10-й элемент будет 100 или -100
                a.append(100 if i % 20 == 0 else -100)
            else:
                a.append(random.randint(-99, 99))
        print("\nИспользуется готовый набор 3: специальный набор, где 100 и -100 - максимальные по модулю")
    
    # Вычисляем максимальное значение по модулю
    max_abs = max(abs(x) for x in a)
    print(f"\nМаксимальное значение по модулю: {max_abs}")
    
    # Находим все значения, которые по модулю равны максимуму
    max_abs_values = []
    for x in a:
        if abs(x) == max_abs:
            max_abs_values.append(x)
    
    print(f"Числа с максимальным модулем: {max_abs_values[:10]}... (всего {len(max_abs_values)})")
    
    # Создаем новую последовательность из 0 и 1
    b = []
    for x in a:
        if abs(x) == max_abs:
            b.append(1)
        else:
            b.append(0)
    
    # Выводим результаты
    print(f"\nПервые 20 элементов исходной последовательности:")
    for i in range(20):
        print(f"a_{i+1:3} = {a[i]:4} (|a|={abs(a[i]):3}) -> b_{i+1:3} = {b[i]}")
    
    print("\nПервые 20 элементов преобразованной последовательности b:")
    for i in range(20):
        print(f"b_{i+1:3} = {b[i]}", end=" ")
        if (i+1) % 10 == 0:
            print()
    
    print("\nПоследние 20 элементов преобразованной последовательности b:")
    for i in range(n-20, n):
        print(f"b_{i+1:3} = {b[i]}", end=" ")
        if (i+1) % 10 == 0:
            print()
    
    # Статистика
    ones_count = sum(b)  # количество единиц равно количеству элементов с максимальным модулем
    zeros_count = n - ones_count
    
    print("\n" + "="*60)
    print("Статистика:")
    print(f"Всего элементов: {n}")
    print(f"Количество единиц (максимальный модуль): {ones_count} ({ones_count/n*100:.1f}%)")
    print(f"Количество нулей: {zeros_count} ({zeros_count/n*100:.1f}%)")
    
    # Проверка - выведем все индексы, где b[i] = 1
    print(f"\nИндексы, где b[i] = 1 (максимальный модуль):")
    indices = [i+1 for i, val in enumerate(b) if val == 1]
    if len(indices) <= 20:
        print(indices)
    else:
        print(f"Первые 10: {indices[:10]}")
        print(f"Последние 10: {indices[-10:]}")
        print(f"Всего: {len(indices)} индексов")
    
    # Дополнительная информация
    print("\nДополнительная информация об исходной последовательности:")
    print(f"Минимальное значение: {min(a)}")
    print(f"Максимальное значение: {max(a)}")
    print(f"Среднее значение: {sum(a)/n:.2f}")
    print(f"Сумма всех элементов: {sum(a)}")
    print(f"Количество положительных чисел: {sum(1 for x in a if x > 0)}")
    print(f"Количество отрицательных чисел: {sum(1 for x in a if x < 0)}")
    print(f"Количество нулей: {sum(1 for x in a if x == 0)}")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
