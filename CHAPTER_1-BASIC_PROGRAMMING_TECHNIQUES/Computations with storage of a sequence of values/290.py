"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
9. Вычисления с хранением последовательности значений

290. Даны действительные числа x1 ,…, x101, y1 ,…, y101. 
Получить действительные x1', ..., x101', y1', ..., y101', преобразовав для получения xi', yi' члены xi, yi по правилу: если они оба отрицательны, то каждый из них увеличить на 0.5; если отрицательно только одно число, то отрицательное число заменить его квадратом; если оба числа неотрицательны, то каждое из них заменить на среднее арифметическое исходных значений.
"""


def main():
    n = 101  # количество пар
    
    print(f"Введите {n} пар действительных чисел (x_i, y_i).")
    
    # Списки для исходных данных
    x = []
    y = []
    
    # Списки для преобразованных данных
    x_prime = []
    y_prime = []
    
    # Ввод данных
    for i in range(n):
        while True:
            try:
                x_val = float(input(f"Введите x_{i+1}: "))
                y_val = float(input(f"Введите y_{i+1}: "))
                
                x.append(x_val)
                y.append(y_val)
                break
            except ValueError:
                print("Ошибка: введите действительные числа.")
    
    # Преобразование пар по правилам
    for i in range(n):
        x_val = x[i]
        y_val = y[i]
        
        # Случай 1: оба отрицательны
        if x_val < 0 and y_val < 0:
            x_prime.append(x_val + 0.5)
            y_prime.append(y_val + 0.5)
        
        # Случай 2: только x отрицательный
        elif x_val < 0 <= y_val:
            x_prime.append(x_val ** 2)
            y_prime.append(y_val)
        
        # Случай 3: только y отрицательный
        elif y_val < 0 <= x_val:
            x_prime.append(x_val)
            y_prime.append(y_val ** 2)
        
        # Случай 4: оба неотрицательны (включая 0)
        else:  # x_val >= 0 and y_val >= 0
            avg = (x_val + y_val) / 2.0
            x_prime.append(avg)
            y_prime.append(avg)
    
    # Вывод результатов
    print("\nРезультаты преобразования:")
    print("\nИндекс | Исходные (x, y) | Преобразованные (x', y')")
    print("-" * 60)
    
    # Выведем только первые 10 и последние 10 пар для компактности
    for i in range(10):
        print(f"{i+1:6d} | ({x[i]:7.3f}, {y[i]:7.3f}) | ({x_prime[i]:7.3f}, {y_prime[i]:7.3f})")
    
    print("...")
    
    for i in range(n-10, n):
        print(f"{i+1:6d} | ({x[i]:7.3f}, {y[i]:7.3f}) | ({x_prime[i]:7.3f}, {y_prime[i]:7.3f})")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
