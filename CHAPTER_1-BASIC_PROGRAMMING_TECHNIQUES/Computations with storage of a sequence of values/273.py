"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
9. Вычисления с хранением последовательности значений

273. Система из 25 материальных точек в пространстве задана с помощью последовательности действительных чисел x1, y1, z1, p1, x2, y2, z2, p2, …, x25, y25, z25, p25, где xi, yi, zi - координаты i-ой точки, а pi – ее вес (i=1, 2, …, 25). Получить координаты центра тяжести системы, а также расстояние от центра тяжести до всех точек системы.
"""


import math

def main():
    n = 25  # количество материальных точек
    
    print(f"Введите данные для {n} материальных точек.")
    print("Для каждой точки введите 4 числа через пробел: x y z p")
    print("где (x, y, z) - координаты, p - вес точки")
    
    # Инициализируем суммы
    sum_p = 0.0
    sum_px = 0.0
    sum_py = 0.0
    sum_pz = 0.0
    
    # Списки для хранения данных точек
    points = []
    
    # Ввод данных
    for i in range(n):
        while True:
            try:
                data = input(f"Точка {i+1}: ").split()
                if len(data) != 4:
                    print("Ошибка: введите ровно 4 числа")
                    continue
                    
                x, y, z, p = map(float, data)
                
                if p <= 0:
                    print("Ошибка: вес должен быть положительным числом")
                    continue
                
                # Сохраняем точку
                points.append((x, y, z, p))
                
                # Обновляем суммы
                sum_p += p
                sum_px += p * x
                sum_py += p * y
                sum_pz += p * z
                
                break
                
            except ValueError:
                print("Ошибка: введите действительные числа")
    
    # Вычисляем координаты центра тяжести
    if sum_p == 0:
        print("Ошибка: суммарный вес равен нулю")
        return
    
    center_x = sum_px / sum_p
    center_y = sum_py / sum_p
    center_z = sum_pz / sum_p
    
    print(f"\nКоординаты центра тяжести системы:")
    print(f"X = {center_x:.6f}")
    print(f"Y = {center_y:.6f}")
    print(f"Z = {center_z:.6f}")
    
    # Вычисляем расстояния от центра тяжести до каждой точки
    print(f"\nРасстояния от центра тяжести до каждой точки:")
    print("№ точки | Расстояние")
    print("-" * 30)
    
    for i, (x, y, z, p) in enumerate(points, 1):
        distance = math.sqrt((x - center_x) ** 2 + 
                             (y - center_y) ** 2 + 
                             (z - center_z) ** 2)
        print(f"{i:7d} | {distance:.6f}")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
