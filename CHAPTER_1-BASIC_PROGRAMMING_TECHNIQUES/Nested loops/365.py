"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
10. Вложенные циклы

365. Даны натуральное число n, символы s1,...,sn. Будем рассматривать слова, образованные символами, входящими в последовательность s1,...,sn (см. задачу 269) считая при этом, что количество символов в каждом слове не превосходит 15. 
а) Найти наибольшую длину символов-палиндромов. (Если палиндромов нет, то ответом должно быть число 0.)
б) Выяснить, верно ли, что каждое слово, не являещееся палиндромом, имеет четную длину.
в) Выяснить, имеются ли два слова, каждое из которых получается переворачиванием другого. 
г) Удалить из s1,...,sn все слова, встречающиеся более двух раз.
"""


import random

def get_input():
    """Выбор способа ввода строки s (последовательности символов)."""
    print("=== Задача 365: Анализ слов в последовательности ===")
    print("Выберите способ ввода:")
    print("1 — Ручной ввод")
    print("2 — Случайная генерация")
    print("3 — Готовые примеры")

    choice = input("Ваш выбор (1/2/3): ").strip()
    while choice not in ('1', '2', '3'):
        choice = input("Выберите 1, 2 или 3: ").strip()

    if choice == '1':
        # Ручной ввод: сначала n, затем строка (или можно ввести строку целиком)
        try:
            n = int(input("Введите натуральное число n: "))
            if n <= 0:
                print("Ошибка: n должно быть положительным.")
                return None
            print(f"Введите строку из {n} символов (включая пробелы):")
            s = input()
            if len(s) != n:
                print(f"Предупреждение: длина введённой строки = {len(s)}. Используем её.")
            return s
        except ValueError:
            print("Ошибка ввода.")
            return None

    elif choice == '2':
        # Случайная генерация строки из слов длиной до 15
        # Генерируем случайное количество слов (от 1 до 10)
        num_words = random.randint(1, 10)
        words = []
        for _ in range(num_words):
            # Длина слова от 1 до 15
            wlen = random.randint(1, 15)
            # Случайные буквы (строчные)
            word = ''.join(random.choices("abcdefghijklmnopqrstuvwxyz", k=wlen))
            words.append(word)
        # Соединяем слова одним или несколькими пробелами
        s = '  '.join(words)  # два пробела для разнообразия
        print(f"\nСгенерировано: {num_words} слов(а)")
        print(f"Строка: {s}")
        return s

    else:
        # Готовые примеры (строки с разными ситуациями)
        examples = [
            "а роза упала на лапу азора",  # слова: "а", "роза", "упала", "на", "лапу", "азора" (палиндромы: "а", "азора"? "азора" не палиндром)
            "шалаш заказ доход",            # все слова палиндромы
            "abc cba ab ba",                 # "abc" и "cba" – перевёртыши, "ab" и "ba" – тоже
            "кот ток топ пот",                # "кот" и "ток" – перевёртыши, "топ" и "пот" – тоже
            "aa bb cc aa bb cc dd",           # слова: "aa", "bb", "cc" встречаются дважды, "dd" один раз. Удалить надо слова более двух раз? здесь все не более двух.
            "aa aa aa bb bb",                  # "aa" встречается три раза -> должно быть удалено, "bb" два раза остаётся.
            "один два два три три три",        # "три" три раза -> удалить, останутся "один" и "два" (дважды)
            "a b c d e"                         # все по одному разу, палиндромы только однобуквенные
        ]
        print("\nГотовые примеры (строки):")
        for i, ex in enumerate(examples, 1):
            preview = ex[:30] + "..." if len(ex) > 30 else ex
            print(f"{i}. {preview}")
        try:
            num = int(input("\nВыберите номер примера: "))
            if 1 <= num <= len(examples):
                return examples[num-1]
            else:
                print("Неверный номер!")
                return None
        except ValueError:
            print("Ошибка ввода.")
            return None

def is_palindrome(word):
    return word == word[::-1]

def main():
    s = get_input()
    if s is None:
        return

    # Разбиваем на слова (убираем лишние пробелы)
    words = s.split()
    print(f"\nИсходная строка: {s}")
    print(f"Слова: {words}")
    print(f"Количество слов: {len(words)}")

    # а) Наибольшая длина слов-палиндромов
    palindromes = [w for w in words if is_palindrome(w)]
    max_pal_len = max((len(w) for w in palindromes), default=0)
    print("\nа) Наибольшая длина слов-палиндромов:", max_pal_len)

    # б) Проверка: каждое непалиндромическое слово имеет чётную длину
    all_even = True
    for w in words:
        if not is_palindrome(w) and len(w) % 2 != 0:
            all_even = False
            break
    print("б) Верно ли, что все непалиндромы имеют чётную длину?", "Да" if all_even else "Нет")

    # в) Есть ли пара слов, являющихся перевёртышами друг друга (различные слова)
    found_pair = False
    nw = len(words)
    for i in range(nw):
        for j in range(i+1, nw):
            if words[i] == words[j][::-1]:
                found_pair = True
                print(f"в) Найдена пара: '{words[i]}' и '{words[j]}'")
                break
        if found_pair:
            break
    if not found_pair:
        print("в) Нет двух различных слов, являющихся перевёртышами друг друга.")

    # г) Удалить все слова, встречающиеся более двух раз
    from collections import Counter
    freq = Counter(words)
    # Оставляем слова, частота которых <= 2
    filtered_words = [w for w in words if freq[w] <= 2]
    # Восстанавливаем строку с пробелами (один пробел между словами)
    result_str = ' '.join(filtered_words)
    print("г) Результат удаления слов, встречающихся >2 раз:")
    print(result_str if result_str else "(пустая строка)")

if __name__ == "__main__":
    main()
    input("\nНажмите Enter, чтобы завершить программу.")
