"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
2. Разветвления
46. Даны действительные числа x, y. 
Если x и y отрицательны, то каждое значение заменить его модулем; 
если отрицательно только одно из них, то оба значения увеличить на 0.5; 
если оба значения неотрицательны и ни одно из них не принадлежит 
отрезку [0.5, 2.0], то оба значения уменьшить в 10 раз; 
в остальных случаях x и y оставить без изменения.
"""


def process_numbers(x: float, y: float) -> tuple[float, float]:
    """Обрабатывает два числа согласно условию задачи.
    
    Args:
        x: Первое число.
        y: Второе число.
    Returns:
        Кортеж из двух обработанных чисел (x, y).
    """
    if x < 0 and y < 0:
        # Оба отрицательны — заменяем модулями
        return abs(x), abs(y)
    if (x < 0 <= y) or (y < 0 <= x):
        # Отрицательно только одно — увеличиваем оба на 0.5
        return x + 0.5, y + 0.5
    if x >= 0 and y >= 0:
        x_in_range = 0.5 <= x <= 2.0
        y_in_range = 0.5 <= y <= 2.0
        if not x_in_range and not y_in_range:
            # Ни одно не принадлежит [0.5, 2.0] — уменьшаем в 10 раз
            return x / 10, y / 10
    # В остальных случаях — без изменений
    return x, y


def main() -> None:
    """Основная функция программы."""
    print("Обработка двух действительных чисел")
    print("-" * 60)

    try:
        x = float(input("Введите число x: "))
        y = float(input("Введите число y: "))

        print("-" * 60)
        print(f"Исходные значения: x = {x}, y = {y}")

        new_x, new_y = process_numbers(x, y)

        print("-" * 60)
        if x < 0 and y < 0:
            print("Оба числа отрицательны → заменены модулями.")
        elif (x < 0 <= y) or (y < 0 <= x):
            print("Отрицательно только одно → оба увеличены на 0.5.")
        elif x >= 0 and y >= 0:
            x_in_range = 0.5 <= x <= 2.0
            y_in_range = 0.5 <= y <= 2.0
            if not x_in_range and not y_in_range:
                print("Оба неотрицательны и вне [0.5, 2.0] → уменьшены в 10 раз.")
            else:
                print("Оба неотрицательны, хотя бы одно в [0.5, 2.0] → без изменений.")
        else:
            print("Неизвестная ситуация (не должна происходить).")

        print(f"Результат: x = {new_x}, y = {new_y}")

    except ValueError:
        print("Ошибка: необходимо вводить числа!")


if __name__ == "__main__":
    main()

input("\nНажмите Enter, чтобы завершить программу.")
