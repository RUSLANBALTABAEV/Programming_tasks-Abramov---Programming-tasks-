def process_numbers(x: float, y: float, z: float) -> tuple[float, float, float]:
    """Обрабатывает три числа по условию задачи.
    
    Если сумма чисел меньше 1 — наименьшее заменяется
    полусуммой двух других.
    Иначе меньшее из x и y заменяется полусуммой двух оставшихся.
    """
