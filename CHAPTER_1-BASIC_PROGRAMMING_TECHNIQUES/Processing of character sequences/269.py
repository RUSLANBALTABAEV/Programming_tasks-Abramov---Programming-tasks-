"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
7. Сочетания цикла и разветвления

269. Даны натуральное число n, символы s1, …, sn. Группы символов, разделенные пробелами (одним или несколькими) и не
содержащие пробелов внутри себя, будем называть словами.
а) Подсчитать количество слов в данной последовательности.
б) Подсчитать количество букв а в последнем слове данной последовательности.
в) Найти количество слов, начинающихся с буквы б.
г) Найти количество слов, у которых первый и последний символы совпадают между собой.
д) Найти какое-нибудь слово, начинающееся с буквы а.
е) Преобразовать данную последовательность, заменяя каждое вхождение слова это на слово то.
ж) Найти длину самого короткого слова.
"""


def main():
    # Ввод данных
    n = int(input("Введите количество символов n: "))
    
    # Считываем n символов
    chars = []
    for i in range(n):
        char = input(f"Введите символ s{i+1}: ")
        if len(char) > 0:
            chars.append(char[0])
        else:
            chars.append(' ')  # Если введена пустая строка, считаем это пробелом
    
    # Преобразуем список символов в строку
    sequence = ''.join(chars)
    
    print(f"\nИсходная последовательность: '{sequence}'")
    print(f"Длина последовательности: {len(sequence)}")
    
    # Разбиваем на слова (группы символов, разделенные пробелами)
    # Фильтруем пустые строки, которые могут возникнуть из-за нескольких пробелов
    words = [word for word in sequence.split(' ') if word != '']
    
    print(f"\nСписок слов: {words}")
    print(f"Количество слов: {len(words)}")
    
    # a) Подсчитать количество слов в данной последовательности
    print(f"\nа) Количество слов: {len(words)}")
    
    # б) Подсчитать количество букв 'a' в последнем слове
    if words:
        last_word = words[-1]
        count_a = last_word.count('a')
        print(f"б) Количество букв 'a' в последнем слове '{last_word}': {count_a}")
    else:
        print("б) В последовательности нет слов")
    
    # в) Найти количество слов, начинающихся с буквы 'b'
    count_start_with_b = sum(1 for word in words if word.startswith('b'))
    print(f"в) Количество слов, начинающихся с 'b': {count_start_with_b}")
    
    # г) Найти количество слов, у которых первый и последний символы совпадают
    count_first_last_same = sum(1 for word in words if len(word) > 0 and word[0] == word[-1])
    print(f"г) Количество слов, у которых первый и последний символы совпадают: {count_first_last_same}")
    
    # д) Найти какое-нибудь слово, начинающееся с буквы 'a'
    word_starting_with_a = next((word for word in words if word.startswith('a')), None)
    if word_starting_with_a:
        print(f"д) Слово, начинающееся с 'a': '{word_starting_with_a}'")
    else:
        print("д) Слов, начинающихся с 'a', не найдено")
    
    # е) Преобразовать данную последовательность, заменяя каждое вхождение слова 'это' на слово 'то'
    # Разбиваем последовательность на слова, заменяем, затем собираем обратно
    words_replaced = []
    for word in words:
        if word == 'это':
            words_replaced.append('то')
        else:
            words_replaced.append(word)
    
    # Собираем обратно в строку с пробелами между словами
    transformed_sequence = ' '.join(words_replaced)
    print(f"е) Преобразованная последовательность: '{transformed_sequence}'")
    
    # ж) Найти длину самого короткого слова
    if words:
        min_length = min(len(word) for word in words)
        # Находим все самые короткие слова
        shortest_words = [word for word in words if len(word) == min_length]
        print(f"ж) Длина самого короткого слова: {min_length}")
        print(f"   Самые короткие слова: {shortest_words}")
    else:
        print("ж) В последовательности нет слов")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
