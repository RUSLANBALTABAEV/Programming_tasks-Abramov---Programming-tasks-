"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
7. Сочетания цикла и разветвления

252. Даны натуральное число n, символы s1, …, sn. Подсчитать:
а) сколько раз среди данных символов встречается символ + и сколько раз символ ∗ ;
б) общее число вхождений символов +, –, * в последовательность s1,...,sn
"""


def main():
    print("=== Задача 252 (строго по условию) ===")
    
    # Ввод данных
    n = int(input("Введите натуральное число n: "))
    
    print(f"Введите {n} символов подряд:")
    symbols = input()
    
    # Проверяем длину введенных символов
    if len(symbols) > n:
        symbols = symbols[:n]
    elif len(symbols) < n:
        print(f"Предупреждение: введено только {len(symbols)} символов")
        n = len(symbols)
    
    # Инициализация счетчиков
    count_plus = 0  # для '+'
    count_star = 0  # для '*'
    count_total = 0 # для '+', '-', '*'
    
    # Обработка каждого символа
    for char in symbols:
        if char == '+':
            count_plus += 1
            count_total += 1
        elif char == '*':
            count_star += 1
            count_total += 1
        elif char == '-':
            count_total += 1
    
    # Вывод результатов
    print("\nа) Результаты:")
    print(f"   Символ '+' встречается {count_plus} раз")
    print(f"   Символ '*' встречается {count_star} раз")
    
    print("\nб) Общее количество вхождений символов '+', '-', '*':")
    print(f"   Всего: {count_total} раз")

if __name__ == "__main__":
    main()
