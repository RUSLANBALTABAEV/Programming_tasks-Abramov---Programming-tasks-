"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
7. Сочетания цикла и разветвления

260. Даны натуральное число n, символы s1, …, sn, среди которых есть двоеточие.
а) Получить все символы, расположенные до первого двоеточия включительно.
б) Получить все символы, расположенные после первого двоеточия.
в) Получить все символы, расположенные между первым и вторым двоеточием. Если второго двоеточия нет, то получить все символы, расположенные после единственного имеющегося двоеточия.
"""


def main():
    print("=== Задача 260 ===")
    print("Обработка последовательности символов с двоеточием")
    print()
    
    # Ввод данных
    try:
        n = int(input("Введите натуральное число n: "))
        if n <= 0:
            print("Ошибка: n должно быть натуральным числом.")
            return
        
        print(f"Введите {n} символов подряд:")
        symbols = input()
        
        # Проверка длины введенной последовательности
        if len(symbols) < n:
            print(f"Предупреждение: введено только {len(symbols)} символов. Будем использовать их.")
            n = len(symbols)
        elif len(symbols) > n:
            print(f"Предупреждение: введено {len(symbols)} символов. Будем использовать первые {n}.")
            symbols = symbols[:n]
        
        # Проверяем наличие двоеточия
        if ':' not in symbols:
            print("Ошибка: по условию среди символов должно быть хотя бы одно двоеточие.")
            return
        
        print(f"\nИсходная последовательность: '{symbols}'")
        print(f"Длина последовательности: {n}")
        
        # Находим позиции двоеточий
        first_colon_pos = symbols.find(':')
        second_colon_pos = symbols.find(':', first_colon_pos + 1)  # Поиск второго двоеточия
        
        # а) Все символы до первого двоеточия включительно
        result_a = symbols[:first_colon_pos + 1]
        print(f"\nа) Символы до первого двоеточия включительно: '{result_a}'")
        print(f"   Длина: {len(result_a)} символов")
        
        # б) Все символы после первого двоеточия
        result_b = symbols[first_colon_pos + 1:]
        print(f"\nб) Символы после первого двоеточия: '{result_b}'")
        print(f"   Длина: {len(result_b)} символов")
        
        # в) Символы между первым и вторым двоеточием (или после первого, если второго нет)
        if second_colon_pos != -1:
            result_c = symbols[first_colon_pos + 1:second_colon_pos]
            print(f"\nв) Символы между первым и вторым двоеточием: '{result_c}'")
            print(f"   Второе двоеточие найдено на позиции {second_colon_pos + 1}")
        else:
            result_c = symbols[first_colon_pos + 1:]
            print(f"\nв) Второго двоеточия нет, взяты все символы после первого двоеточия: '{result_c}'")
            print("   (так как второго двоеточия нет)")
        
        print(f"   Длина: {len(result_c)} символов")
        
        # Дополнительная информация
        colon_count = symbols.count(':')
        print(f"\nДополнительно:")
        print(f"   Всего двоеточий в последовательности: {colon_count}")
        print(f"   Первое двоеточие на позиции: {first_colon_pos + 1}")
        if second_colon_pos != -1:
            print(f"   Второе двоеточие на позиции: {second_colon_pos + 1}")
        else:
            print(f"   Второго двоеточия нет")
            
    except ValueError:
        print("Ошибка: n должно быть натуральным числом.")
        return

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
