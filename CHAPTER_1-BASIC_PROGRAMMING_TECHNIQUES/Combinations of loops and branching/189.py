"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
7. Сочетания цикла и разветвления

189.Даны натуральное число n, действительные числа a1, … , an.
В последовательности a1, … , an все неотрицательные члены, не принадлежащие отрезку [1, 2], заменить на единицу. Кроме того, получить число отрицательных членов и число членов, принадлежащих отрезку [1, 2].
"""


def main():
    # Ввод количества чисел
    n = int(input("Введите натуральное число n: "))
    
    if n <= 0:
        print("Ошибка: n должно быть натуральным числом!")
        return
    
    # Ввод последовательности действительных чисел
    a = []
    print(f"Введите {n} действительных чисел:")
    for i in range(n):
        while True:
            try:
                num = float(input(f"a[{i+1}]: "))
                a.append(num)
                break
            except ValueError:
                print("Пожалуйста, введите число!")
    
    # Создаем измененную последовательность
    b = []
    negative_count = 0
    interval_count = 0
    
    for num in a:
        if num < 0:
            # Отрицательные не меняем
            b.append(num)
            negative_count += 1
        else:  # неотрицательные
            if num < 1 or num > 2:
                # Не принадлежит отрезку [1,2] -> заменяем на 1
                b.append(1.0)
            else:
                # Принадлежит отрезку [1,2] -> оставляем без изменений
                b.append(num)
                interval_count += 1
    
    # Вывод результатов
    print("\nРезультаты:")
    print("Измененная последовательность (неотрицательные, не принадлежащие [1,2], заменены на 1):")
    for i in range(n):
        print(f"b[{i+1}] = {b[i]:.6f}")
    
    print(f"\nЧисло отрицательных членов: {negative_count}")
    print(f"Число членов, принадлежащих отрезку [1, 2]: {interval_count}")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
