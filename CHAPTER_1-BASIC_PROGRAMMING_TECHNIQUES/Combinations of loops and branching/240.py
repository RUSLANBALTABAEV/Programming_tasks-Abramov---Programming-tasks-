"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
7. Сочетания цикла и разветвления

240. Для любого целого k обозначим количество цифр в его десятичной записи через Ц(k).
а) Дано натуральное число n. Вычислить Ц(1) / (1 * 1) + Ц(2) / (2 * 2) + ... + Ц(n) / (n * n).
б) Даны натуральное число n, действительное число x.
Вычислить
10^Ц(1) / 1 * (1 - x) + 10 ^ Ц(2) / 2 * (1 - x) ^ 2 + ... + 10 ^ Ц(n) / n * (1 - x) ^ n.
"""


def main():
    print("Выберите подзадачу: a или b")
    choice = input().strip().lower()
    
    if choice == 'a':
        n = int(input("Введите натуральное число n: "))
        total = 0.0
        for k in range(1, n + 1):
            Lk = len(str(k))          # Количество цифр в k
            total += Lk / (k ** 2)
        print(f"Сумма: {total}")
    
    elif choice == 'b':
        n = int(input("Введите натуральное число n: "))
        x = float(input("Введите действительное число x: "))
        total = 0.0
        for k in range(1, n + 1):
            Lk = len(str(k))          # Количество цифр в k
            term = (10 ** Lk) / k * ((1 - x) ** k)
            total += term
        print(f"Сумма: {total}")
    
    else:
        print("Неверный выбор")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
