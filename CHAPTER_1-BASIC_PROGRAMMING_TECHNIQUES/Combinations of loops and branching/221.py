"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
7. Сочетания цикла и разветвления

221. Даны натуральное число n, действительные числа x1, …, xn. 
Получить (1+r)/(1+s), где r – сумма всех тех членов
последовательности x1, …, xn, которые не превосходят 1, а s – сумма
членов, больших 1.
"""


def main():
    # Ввод натурального числа n
    n = int(input("Введите натуральное число n: "))
    
    # Ввод последовательности из n действительных чисел
    print(f"Введите {n} действительных чисел через пробел:")
    numbers = list(map(float, input().split()))
    
    if len(numbers) != n:
        print(f"Ошибка: введено {len(numbers)} чисел вместо {n}")
        return
    
    # Инициализация сумм
    r = 0.0  # сумма чисел <= 1
    s = 0.0  # сумма чисел > 1
    
    # Вычисление сумм r и s
    for x in numbers:
        if x <= 1:
            r += x
        else:
            s += x
    
    # Вычисление результата
    result = (1 + r) / (1 + s)
    
    # Вывод результатов
    print("\nРезультаты вычислений:")
    print(f"Числа, не превосходящие 1 (<=1): {[x for x in numbers if x <= 1]}")
    print(f"Сумма r (чисел <=1) = {r:.6f}")
    print(f"Числа, большие 1 (>1): {[x for x in numbers if x > 1]}")
    print(f"Сумма s (чисел >1) = {s:.6f}")
    print(f"Выражение (1+r)/(1+s) = (1+{r:.6f})/(1+{s:.6f}) = {result:.6f}")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
