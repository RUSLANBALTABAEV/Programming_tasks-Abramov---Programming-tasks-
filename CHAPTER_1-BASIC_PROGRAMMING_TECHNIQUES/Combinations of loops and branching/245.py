"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
7. Сочетания цикла и разветвления

245. Даны натуральное число n, целые числа a1, …, a30, b1, …,
b40, c1, …, cn. Верно ли, что отрицательный член в последовательности
c1, …, cn, встречается раньше, чем в последовательностях a1, …, a30 и
b1, …, b40? Предполагается, что каждая из последовательностей
содержит хотя бы один отрицательный член.
"""


def main():
    # Ввод данных
    n = int(input("Введите натуральное число n (длина последовательности c): "))
    
    print("Введите 30 целых чисел для последовательности a (через пробел):")
    a = list(map(int, input().split()))
    if len(a) != 30:
        print("Ошибка: необходимо ввести ровно 30 чисел для последовательности a")
        return
    
    print("Введите 40 целых чисел для последовательности b (через пробел):")
    b = list(map(int, input().split()))
    if len(b) != 40:
        print("Ошибка: необходимо ввести ровно 40 чисел для последовательности b")
        return
    
    print(f"Введите {n} целых чисел для последовательности c (через пробел):")
    c = list(map(int, input().split()))
    if len(c) != n:
        print(f"Ошибка: необходимо ввести ровно {n} чисел для последовательности c")
        return
    
    # Поиск первого отрицательного элемента в каждой последовательности
    def first_negative_index(seq):
        for i, val in enumerate(seq):
            if val < 0:
                return i + 1  # +1 для нумерации с 1
        return float('inf')  # Если отрицательных нет (по условию есть, но для надежности)
    
    index_a = first_negative_index(a)
    index_b = first_negative_index(b)
    index_c = first_negative_index(c)
    
    # Проверка условия
    if index_c < index_a and index_c < index_b:
        print("Верно: отрицательный член в последовательности c встречается раньше,")
        print(f"чем в последовательностях a (позиция {index_a}) и b (позиция {index_b}).")
        print(f"Первый отрицательный в c на позиции {index_c}.")
    else:
        print("Неверно: отрицательный член в последовательности c НЕ встречается раньше,")
        print(f"чем в последовательностях a (позиция {index_a}) и b (позиция {index_b}).")
        print(f"Первый отрицательный в c на позиции {index_c}.")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
