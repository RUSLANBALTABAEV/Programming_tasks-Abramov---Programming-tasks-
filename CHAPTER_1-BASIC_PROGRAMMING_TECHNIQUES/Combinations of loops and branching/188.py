"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
7. Сочетания цикла и разветвления

188. Даны натуральное число n, действительные числа x1, … , xn.
В последовательности x1, … , xn все члены, меньшие двух, заменить нулями. Кроме того, получить сумму членов, принадлежащих отрезку
[3,7], а также число таких членов.
"""


def main():
    # Ввод количества чисел
    n = int(input("Введите натуральное число n: "))
    
    if n <= 0:
        print("Ошибка: n должно быть натуральным числом!")
        return
    
    # Ввод последовательности действительных чисел
    x = []
    print(f"Введите {n} действительных чисел:")
    for i in range(n):
        while True:
            try:
                num = float(input(f"x[{i+1}]: "))
                x.append(num)
                break
            except ValueError:
                print("Пожалуйста, введите число!")
    
    # Создаем измененную последовательность (все члены, меньшие двух, заменяем нулями)
    y = []
    for num in x:
        if num < 2:
            y.append(0.0)
        else:
            y.append(num)
    
    # Вычисляем сумму и количество членов в отрезке [3, 7] (включительно)
    sum_in_range = 0.0
    count_in_range = 0
    
    for num in y:
        if 3 <= num <= 7:
            sum_in_range += num
            count_in_range += 1
    
    # Вывод результатов
    print("\nРезультаты:")
    print("Измененная последовательность (члены < 2 заменены на 0):")
    for i in range(n):
        print(f"y[{i+1}] = {y[i]:.6f}")
    
    print(f"\nСумма членов, принадлежащих отрезку [3, 7]: {sum_in_range:.6f}")
    print(f"Количество членов, принадлежащих отрезку [3, 7]: {count_in_range}")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
