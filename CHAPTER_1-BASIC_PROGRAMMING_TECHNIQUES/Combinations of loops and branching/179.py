"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
7. Сочетания цикла и разветвления

179. Даны натуральные числа n, q1, … , qn. Найти те члены qi последовательности q1, … , qn, которые 
а) являются удвоенными нечетными числами;
б) при делении на 7 дают остаток 1, 2 или 5;
в) обладают тем свойством, что корни уравнения x2 + 3qi – 5 действительны и положительны.
"""


import math

def main():
    # Ввод количества чисел
    n = int(input("Введите натуральное число n: "))
    
    if n <= 0:
        print("Ошибка: n должно быть натуральным числом!")
        return
    
    # Ввод последовательности чисел
    q = []
    print(f"Введите {n} натуральных чисел:")
    for i in range(n):
        while True:
            try:
                num = int(input(f"q[{i+1}]: "))
                if num > 0:
                    q.append(num)
                    break
                else:
                    print("Число должно быть натуральным (положительным целым)!")
            except ValueError:
                print("Пожалуйста, введите целое число!")
    
    # Инициализация списков для результатов
    result_a = []  # удвоенные нечетные числа
    result_b = []  # дают остаток 1, 2 или 5 при делении на 7
    result_c = []  # корни уравнения x² + 3q_i - 5 действительны и положительны
    
    # Перебираем все элементы последовательности
    for i in range(n):
        qi = q[i]
        i_index = i + 1  # порядковый номер
        
        # а) Удвоенные нечетные числа
        # qi = 2 * (нечетное) => qi/2 должно быть нечетным
        if qi % 2 == 0:  # должно быть четным
            half = qi // 2
            if half % 2 != 0:  # и половина должна быть нечетной
                result_a.append(qi)
        
        # б) При делении на 7 дают остаток 1, 2 или 5
        remainder = qi % 7
        if remainder == 1 or remainder == 2 or remainder == 5:
            result_b.append(qi)
        
        # в) Корни уравнения x² + 3q_i - 5 действительны и положительны
        # Уравнение: x² + 3qi - 5 = 0 => x² = 5 - 3qi
        # Корни: x = ±√(5 - 3qi)
        # Действительны: 5 - 3qi >= 0 => qi <= 5/3
        # Положительны: √(5 - 3qi) > 0 и -√(5 - 3qi) > 0
        # Второе условие невозможно для qi < 5/3, так как -√(5 - 3qi) < 0
        # При qi = 5/3 корни равны 0, что не является положительным
        
        # Если интерпретировать как "действительны и неотрицательны":
        # Тогда при qi = 5/3 (не целое) корни = 0, но qi должно быть натуральным
        
        # Если интерпретировать как "хотя бы один корень положительный":
        # Тогда нужно 5 - 3qi > 0 и √(5 - 3qi) > 0
        # Это выполняется при qi < 5/3, т.е. для натуральных qi = 1
        
        # Рассмотрим обе интерпретации:
        
        # Интерпретация 1: оба корня положительны (решений нет для натуральных чисел)
        # Интерпретация 2: действительны и хотя бы один положителен
        # Выберем интерпретацию 2, так как она дает нетривиальные результаты
        
        if qi < 5/3:  # qi = 1 (так как qi натуральное)
            # Для qi=1: x² = 2, корни √2 ≈ 1.414 и -√2 ≈ -1.414
            # Есть один положительный корень
            result_c.append(qi)
    
    # Вывод результатов
    print("\nРезультаты:")
    
    print(f"а) Удвоенные нечетные числа: {result_a}")
    print("   (числа вида 2*(2k+1), т.е. делятся на 2, но не делятся на 4)")
    
    print(f"б) Числа, дающие остаток 1, 2 или 5 при делении на 7: {result_b}")
    
    print(f"в) Числа, для которых корни уравнения x² + 3q_i - 5 действительны и хотя бы один положителен: {result_c}")
    print("   (интерпретация: уравнение x² = 5 - 3q_i имеет действительные корни, и √(5 - 3q_i) > 0)")
    
    # Дополнительная информация
    print("\nДетальный анализ условия (в):")
    print("Уравнение: x² + 3q_i - 5 = 0")
    print("Преобразуем: x² = 5 - 3q_i")
    print("Условие действительности: 5 - 3q_i ≥ 0 => q_i ≤ 5/3 ≈ 1.67")
    print("Для натуральных q_i это означает q_i = 1")
    print("При q_i = 1: x² = 2, корни x ≈ ±1.414")
    print("Есть один положительный корень (√2 ≈ 1.414)")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
