"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
7. Сочетания цикла и разветвления

202.Даны натуральное число n, действительные числа a1, … , an.
а) Верно ли, что отрицательных членов в последовательности a1, … , an больше, чем положительных?
б) Верно ли, что наибольший член последовательности a1, … , an по модулю больше единицы?
"""


def check_conditions(n, a):
    """
    n: натуральное число (длина последовательности)
    a: список действительных чисел [a₁, a₂, ..., aₙ]
    Возвращает два булевых значения для условий (a) и (б).
    """
    # Подсчёт положительных и отрицательных чисел
    positive_count = sum(1 for x in a if x > 0)
    negative_count = sum(1 for x in a if x < 0)
    # (a) Отрицательных больше, чем положительных?
    condition_a = negative_count > positive_count

    # (б) Наибольший член по модулю больше единицы?
    max_abs = max(abs(x) for x in a)
    condition_b = max_abs > 1

    return condition_a, condition_b


# Пример использования
n = 5
a = [1.5, -2.3, 4.7, 0.8, -3.2]

result_a, result_b = check_conditions(n, a)
print(f"а) Отрицательных больше, чем положительных? {result_a}")
print(f"б) Наибольший член по модулю больше единицы? {result_b}")
