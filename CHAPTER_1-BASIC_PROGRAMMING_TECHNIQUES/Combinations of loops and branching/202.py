"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
7. Сочетания цикла и разветвления

202. Даны натуральное число n, действительные числа a1, … , an.
а) Верно ли, что отрицательных членов в последовательности a1, … , an больше, чем положительных?
б) Верно ли, что наибольший член последовательности a1, … , an по модулю больше единицы?
"""

def check_conditions(n, a):
    """
    n: натуральное число (длина последовательности)
    a: список действительных чисел [a₁, a₂, ..., aₙ]
    Возвращает два булевых значения для условий (a) и (б).
    """
    # Подсчёт положительных и отрицательных чисел
    positive_count = sum(1 for x in a if x > 0)
    negative_count = sum(1 for x in a if x < 0)
    zero_count = sum(1 for x in a if x == 0)
    
    # (a) Отрицательных больше, чем положительных?
    condition_a = negative_count > positive_count

    # (б) Наибольший член по модулю больше единицы?
    max_abs = max(abs(x) for x in a)
    condition_b = max_abs > 1

    # Дополнительная информация для вывода
    stats = {
        "positive_count": positive_count,
        "negative_count": negative_count,
        "zero_count": zero_count,
        "max_abs": max_abs
    }
    
    return condition_a, condition_b, stats


def main():
    """
    Основная функция программы с выбором режима работы:
    1. Использовать пример из кода
    2. Ввести свои данные
    """
    print("Программа проверки свойств последовательности чисел")
    print("=" * 60)
    
    choice = input("Выберите режим:\n1 - Использовать пример из кода\n2 - Ввести свои данные\nВведите 1 или 2: ")
    
    if choice == '1':
        # Используем пример из кода
        n = 5
        a = [1.5, -2.3, 4.7, 0.8, -3.2]
        print(f"\nИспользуется пример: n = {n}, a = {a}")
    elif choice == '2':
        # Ввод данных от пользователя
        try:
            n = int(input("\nВведите натуральное число n: "))
            if n <= 0:
                print("Ошибка: n должно быть натуральным числом!")
                return
            
            a = []
            for i in range(n):
                value = float(input(f"Введите a_{i+1}: "))
                a.append(value)
            print(f"\nВведена последовательность: a = {a}")
        except ValueError:
            print("Ошибка ввода! Убедитесь, что вводите числа в правильном формате.")
            return
    else:
        print("Неверный выбор. Программа завершена.")
        return
    
    # Проверка условий
    condition_a, condition_b, stats = check_conditions(n, a)
    
    # Вывод результатов
    print("\nРезультаты проверки:")
    print("-" * 40)
    
    # Дополнительная статистика
    print(f"Статистика последовательности:")
    print(f"  Положительных чисел: {stats['positive_count']}")
    print(f"  Отрицательных чисел: {stats['negative_count']}")
    print(f"  Нулей: {stats['zero_count']}")
    print(f"  Наибольший модуль: {stats['max_abs']:.4f}")
    
    print("\nОтветы на вопросы:")
    # а) Верно ли, что отрицательных членов больше, чем положительных?
    if condition_a:
        print(f"а) Да, отрицательных членов больше, чем положительных.")
    else:
        print(f"а) Нет, отрицательных членов не больше, чем положительных.")
    
    # б) Верно ли, что наибольший член по модулю больше единицы?
    if condition_b:
        print(f"б) Да, наибольший член по модулю больше единицы.")
    else:
        print(f"б) Нет, наибольший член по модулю не больше единицы.")
    
    print("=" * 60)


if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
