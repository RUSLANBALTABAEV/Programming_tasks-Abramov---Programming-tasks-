"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
7. Сочетания цикла и разветвления

194. Даны натуральное число n, целые числа a1, x1, … , xn. Если
в последовательности x1, … , xn есть хотя бы один член, равный a, то
получить сумму всех членов, следующих за первым таким членом; в
противном случае ответом должно быть число –10.
"""


def main():
    # Ввод натурального числа n
    n = int(input("Введите натуральное число n: "))
    
    if n <= 0:
        print("Ошибка: n должно быть натуральным числом!")
        return
    
    # Ввод целого числа a
    a = int(input("Введите целое число a: "))
    
    # Ввод последовательности x₁, ..., xₙ
    x = []
    print(f"Введите {n} целых чисел:")
    for i in range(n):
        while True:
            try:
                num = int(input(f"x[{i+1}]: "))
                x.append(num)
                break
            except ValueError:
                print("Пожалуйста, введите целое число!")
    
    # Поиск первого члена, равного a
    found_index = -1
    for i in range(n):
        if x[i] == a:
            found_index = i
            break
    
    # Вычисление результата
    if found_index == -1:
        result = -10  # не найден элемент, равный a
    else:
        # Сумма элементов после найденного
        if found_index == n - 1:  # последний элемент
            result = 0
        else:
            result = sum(x[found_index + 1:])
    
    # Вывод результатов
    print("\nРезультат:")
    print(f"Первый элемент, равный {a}, имеет индекс {found_index + 1 if found_index != -1 else 'не найден'}")
    
    if found_index != -1:
        if found_index == n - 1:
            print("Элемент является последним, поэтому сумма следующих за ним членов: 0")
        else:
            print(f"Сумма всех членов после первого элемента, равного {a}: {result}")
    else:
        print(f"Элемент, равный {a}, не найден. Ответ: {result}")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
