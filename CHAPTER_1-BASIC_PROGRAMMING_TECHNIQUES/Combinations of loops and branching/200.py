"""
ГЛАВА 1
ОСНОВНЫЕ ПРИЁМЫ ПРОГРАММИРОВАНИЯ
7. Сочетания цикла и разветвления

200.Даны целые числа a, n, x1, … , xn ( n > 0 ). Определить, каким по счету идет в последовательности x1, … , xn член, равный a.
Если такого члена нет, то ответом должно быть число 0.
"""


def main():
    # Ввод целого числа a
    a = int(input("Введите целое число a: "))
    
    # Ввод натурального числа n (n > 0)
    n = int(input("Введите натуральное число n (n > 0): "))
    
    if n <= 0:
        print("Ошибка: n должно быть больше 0!")
        return
    
    # Ввод последовательности x₁, ..., xₙ
    x = []
    print(f"Введите {n} целых чисел:")
    for i in range(n):
        while True:
            try:
                num = int(input(f"x[{i+1}]: "))
                x.append(num)
                break
            except ValueError:
                print("Пожалуйста, введите целое число!")
    
    # Поиск первого вхождения a
    position = 0  # по умолчанию 0, если не найдено
    for i in range(n):
        if x[i] == a:
            position = i + 1  # позиция в последовательности (начиная с 1)
            break
    
    # Вывод результата
    print(f"\nРезультат: {position}")

if __name__ == "__main__":
    main()
    input("\n\nНажмите Enter, чтобы завершить программу.")
